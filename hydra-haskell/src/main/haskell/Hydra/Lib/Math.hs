module Hydra.Lib.Math where


hsNeg :: Int -> Int
hsNeg = negate

hsAdd :: Int -> Int -> Int
hsAdd x y = x + y

hsSub :: Int -> Int -> Int
hsSub x y = x - y

hsMul :: Int -> Int -> Int
hsMul x y = x * y

hsDiv :: Int -> Int -> Int
hsDiv = div

hsMod :: Int -> Int -> Int
hsMod = mod

hsRem :: Int -> Int -> Int
hsRem = rem
