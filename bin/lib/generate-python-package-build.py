#!/usr/bin/env python3
"""Generate a standalone pyproject.toml for one Hydra Python distribution
package under dist/python/<pkg>/.

Each emitted build is self-contained: from `dist/python/<pkg>/`, running
`uv build` (or `python -m build`) produces a wheel + sdist and `pip install`
resolves dependencies. The wheel installs files under
site-packages/hydra/<subpath>/ as a PEP 420 implicit namespace package,
so multiple Hydra wheels can coexist and contribute to a shared `hydra`
import namespace.

Inputs:
  packages/<pkg>/package.json (read for name, description, dependencies)
  hydra.json                   (read for currentVersion)

Outputs:
  dist/python/<pkg>/pyproject.toml
"""

from __future__ import annotations

import argparse
import json
import os
import shutil
import sys


HOMEPAGE = "https://github.com/CategoricalData/hydra"
LICENSE = "Apache-2.0"
AUTHOR_NAME = "Joshua Shinavier"
AUTHOR_EMAIL = "josh@fortytwo.net"
PYTHON_REQUIRES = ">=3.12"

def load_overlay_build_config(repo_root: str, name: str) -> dict:
    """Load overlay/python/<pkg>/build.json — the encoded
    hydra.python.pyproject.PyProjectBuildConfiguration for this package (#511).
    Returns {} when absent (most packages have no overlay build config).

    INTERIM: reads the JSON directly. The build.json format is the canonical
    encoding of hydra.python.pyproject.PyProjectBuildConfiguration; when the build
    system is nativized (#416) this hand parse is replaced by a generated
    hydra.decode.pyproject decoder. The on-disk format does not change.
    """
    path = os.path.join(repo_root, "overlay", "python", name, "build.json")
    if not os.path.isfile(path):
        return {}
    with open(path) as f:
        return json.load(f)


def _scope_tag(scope_obj) -> str:
    """The single variant key of an encoded DependencyScope, or 'api' if absent."""
    if not scope_obj:
        return "api"
    return next(iter(scope_obj.keys()))


def _pep440(version_obj) -> str:
    """Render an encoded VersionSpecifier to a PEP 440 version constraint suffix.
    {"exact":"1.0"} -> "==1.0"; {"any":{}} -> ""; {"atLeast":"7.0"} -> ">=7.0";
    {"range":{"lowerInclusive":"3.7","upperExclusive":"4.0"}} -> ">=3.7,<4.0"."""
    if "exact" in version_obj:
        return f"=={version_obj['exact']}"
    if "atLeast" in version_obj:
        return f">={version_obj['atLeast']}"
    if "range" in version_obj:
        r = version_obj["range"]
        parts = []
        if r.get("lowerInclusive"):
            parts.append(f">={r['lowerInclusive']}")
        if r.get("upperExclusive"):
            parts.append(f"<{r['upperExclusive']}")
        return ",".join(parts)
    return ""  # any


def _pyproject_requirement(dep: dict) -> str:
    """An encoded hydra.packaging.PackageDependency -> a PEP 508 requirement string,
    e.g. {"name":"gremlinpython","version":{"range":...}} -> "gremlinpython>=3.7,<4.0"."""
    return dep["name"] + _pep440(dep.get("version", {}))


def render_pyproject(name: str, description: str, version: str, deps: list[str],
                     readme_rel: str | None, overlay: dict | None = None) -> str:
    overlay = overlay or {}
    # Partition overlay deps by scope: api/runtime -> [project.dependencies];
    # test -> [project.optional-dependencies] test extra (#511).
    runtime_extra: list[str] = []
    test_extra: list[str] = []
    for d in overlay.get("dependencies", []):
        req = _pyproject_requirement(d)
        if _scope_tag(d.get("scope")) == "test":
            test_extra.append(req)
        else:
            runtime_extra.append(req)

    dep_lines = []
    # Hydra inter-package deps (from package.json), pinned to the current version.
    for dep in deps:
        dep_lines.append(f'    "{dep} == {version}",')
    # Third-party runtime deps from the overlay build.json.
    for req in runtime_extra:
        dep_lines.append(f'    "{req}",')

    if dep_lines:
        deps_block = "dependencies = [\n" + "\n".join(dep_lines) + "\n]"
    else:
        deps_block = "dependencies = []"

    # Optional (test) dependencies become a PEP 621 optional-dependencies extra.
    if test_extra:
        test_lines = "\n".join(f'    "{r}",' for r in test_extra)
        deps_block += (
            "\n\n[project.optional-dependencies]\n"
            "test = [\n" + test_lines + "\n]")

    # Escape any double-quote characters in description so they don't break
    # the generated TOML string literal.
    safe_desc = description.replace('"', '\\"')

    if readme_rel:
        readme_line = f'readme = "{readme_rel}"\n'
    else:
        # No packages/<pkg>/README.md exists. Set an inline readme from the
        # package description so PyPI/twine see a long_description and the
        # PyPI package page isn't blank. Hatchling accepts the inline form.
        readme_line = (
            f'readme = {{ text = "{safe_desc}", content-type = "text/markdown" }}\n'
        )

    return f"""# Generated file. Do not edit.
# Regenerated by bin/lib/generate-python-package-build.py from
# packages/{name}/package.json and hydra.json (currentVersion). Each
# dist/python/<pkg>/ is a standalone Python distribution: from this directory,
# `uv build` (or `python -m build`) produces a wheel + sdist, and `pip install`
# resolves transitive Hydra dependencies declared below.
#
# All Hydra wheels participate in the `hydra` namespace via PEP 420 implicit
# namespace packages: there is no top-level hydra/__init__.py, so multiple
# wheels can install side-by-side and merge their hydra.* contents at
# import time. Subpackages (hydra/core/, hydra/rdf/syntax/, etc.) DO have
# regular __init__.py files; only the namespace root is implicit.

[project]
name = "{name}"
version = "{version}"
description = "{safe_desc}"
{readme_line}requires-python = "{PYTHON_REQUIRES}"
license = {{ text = "{LICENSE}" }}
authors = [
    {{ name = "{AUTHOR_NAME}", email = "{AUTHOR_EMAIL}" }},
]
{deps_block}

[project.urls]
Homepage = "{HOMEPAGE}"
Repository = "{HOMEPAGE}"

[build-system]
requires = ["hatchling"]
build-backend = "hatchling.build"

[tool.hatch.build.targets.wheel]
packages = ["src/main/python/hydra"]

# Bundle LICENSE + NOTICE into the published wheel + sdist so the artifact
# carries the Apache-2.0 license text and project NOTICE, not just the metadata
# declaration above. Hatchling routes `license-files` into the wheel's
# .dist-info/licenses/ directory (PEP 639). The files are copied into this
# package dir by generate-python-package-build.py.
[tool.hatch.build]
license-files = ["LICENSE", "NOTICE"]
"""


def main() -> int:
    p = argparse.ArgumentParser(description=__doc__.splitlines()[0] if __doc__ else None)
    p.add_argument("package", help="Package name (e.g. hydra-kernel)")
    p.add_argument(
        "--repo-root",
        default=os.environ.get("HYDRA_ROOT_DIR"),
        help="Hydra worktree root (default: $HYDRA_ROOT_DIR)",
    )
    p.add_argument(
        "--out-dir",
        help="Override output directory (default: <repo-root>/dist/python/<package>)",
    )
    args = p.parse_args()

    if not args.repo_root:
        print("error: --repo-root or $HYDRA_ROOT_DIR is required", file=sys.stderr)
        return 2

    pkg_json_path = os.path.join(args.repo_root, "packages", args.package, "package.json")
    if not os.path.isfile(pkg_json_path):
        print(f"error: no such package.json: {pkg_json_path}", file=sys.stderr)
        return 1

    with open(pkg_json_path) as f:
        meta = json.load(f)

    pkg_name = meta.get("name") or args.package
    description = meta.get("description") or pkg_name
    deps = list(meta.get("dependencies") or [])

    # hydra.json:currentVersion is the single source of truth (the standalone
    # VERSION file was retired in #347).
    with open(os.path.join(args.repo_root, "hydra.json")) as f:
        version = json.load(f)["currentVersion"]

    out_dir = args.out_dir or os.path.join(args.repo_root, "dist", "python", args.package)
    os.makedirs(out_dir, exist_ok=True)

    # Reference packages/<pkg>/README.md only if it exists. Most kernel-set
    # packages don't have one; pyproject.toml's `readme` field requires the
    # file to exist if it's referenced.
    #
    # The README must be COPIED into the package dir and referenced as a
    # package-local path ("README.md"), not as ../../../packages/<pkg>/README.md.
    # A path that escapes the package root works in the monorepo but is absent
    # from the published sdist, so any from-sdist build (conda-forge, strict
    # pip) fails with "Readme file does not exist" during metadata generation.
    readme_src = os.path.join(args.repo_root, "packages", args.package, "README.md")
    readme_rel: str | None
    if os.path.isfile(readme_src):
        shutil.copyfile(readme_src, os.path.join(out_dir, "README.md"))
        readme_rel = "README.md"
    else:
        readme_rel = None

    # Copy LICENSE + NOTICE into the package dir so the `license-files` entries
    # resolve in a standalone/published build. Like the README, these must be
    # package-local — a path escaping the package root is absent from the sdist.
    for fname in ("LICENSE", "NOTICE"):
        shutil.copyfile(os.path.join(args.repo_root, fname), os.path.join(out_dir, fname))

    overlay = load_overlay_build_config(args.repo_root, args.package)

    pyproject_path = os.path.join(out_dir, "pyproject.toml")
    with open(pyproject_path, "w") as f:
        f.write(render_pyproject(pkg_name, description, version, deps, readme_rel, overlay))

    print(f"  wrote {pyproject_path}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
