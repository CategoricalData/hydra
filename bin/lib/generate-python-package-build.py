#!/usr/bin/env python3
"""Generate a standalone pyproject.toml for one Hydra Python distribution
package under dist/python/<pkg>/.

Each emitted build is self-contained: from `dist/python/<pkg>/`, running
`uv build` (or `python -m build`) produces a wheel + sdist and `pip install`
resolves dependencies. The wheel installs files under
site-packages/hydra/<subpath>/ as a PEP 420 implicit namespace package,
so multiple Hydra wheels can coexist and contribute to a shared `hydra`
import namespace.

Inputs:
  packages/<pkg>/package.json (read for name, description, dependencies)
  VERSION                      (read for the version string)

Outputs:
  dist/python/<pkg>/pyproject.toml
"""

from __future__ import annotations

import argparse
import json
import os
import sys


HOMEPAGE = "https://github.com/CategoricalData/hydra"
LICENSE = "Apache-2.0"
AUTHOR_NAME = "Joshua Shinavier"
AUTHOR_EMAIL = "josh@fortytwo.net"
PYTHON_REQUIRES = ">=3.12"

# Per-package external (non-Hydra) PyPI dependencies. Currently empty for
# every package: cypher/gql/RDF native bindings live in bindings/ once that
# subtree exists, so no third-party Python deps are pulled into the kernel
# or its siblings.
EXTERNAL_DEPS: dict[str, list[str]] = {}


def render_pyproject(name: str, description: str, version: str, deps: list[str], readme_rel: str | None) -> str:
    dep_lines = []
    for dep in deps:
        dep_lines.append(f'    "{dep} == {version}",')
    for ext in EXTERNAL_DEPS.get(name, []):
        dep_lines.append(f'    "{ext}",')

    if dep_lines:
        deps_block = "dependencies = [\n" + "\n".join(dep_lines) + "\n]"
    else:
        deps_block = "dependencies = []"

    # Escape any double-quote characters in description so they don't break
    # the generated TOML string literal.
    safe_desc = description.replace('"', '\\"')

    if readme_rel:
        readme_line = f'readme = "{readme_rel}"\n'
    else:
        # No packages/<pkg>/README.md exists. Set an inline readme from the
        # package description so PyPI/twine see a long_description and the
        # PyPI package page isn't blank. Hatchling accepts the inline form.
        readme_line = (
            f'readme = {{ text = "{safe_desc}", content-type = "text/markdown" }}\n'
        )

    return f"""# Generated file. Do not edit.
# Regenerated by bin/lib/generate-python-package-build.py from
# packages/{name}/package.json and the worktree's VERSION file. Each
# dist/python/<pkg>/ is a standalone Python distribution: from this directory,
# `uv build` (or `python -m build`) produces a wheel + sdist, and `pip install`
# resolves transitive Hydra dependencies declared below.
#
# All Hydra wheels participate in the `hydra` namespace via PEP 420 implicit
# namespace packages: there is no top-level hydra/__init__.py, so multiple
# wheels can install side-by-side and merge their hydra.* contents at
# import time. Subpackages (hydra/core/, hydra/rdf/syntax/, etc.) DO have
# regular __init__.py files; only the namespace root is implicit.

[project]
name = "{name}"
version = "{version}"
description = "{safe_desc}"
{readme_line}requires-python = "{PYTHON_REQUIRES}"
license = {{ text = "{LICENSE}" }}
authors = [
    {{ name = "{AUTHOR_NAME}", email = "{AUTHOR_EMAIL}" }},
]
{deps_block}

[project.urls]
Homepage = "{HOMEPAGE}"
Repository = "{HOMEPAGE}"

[build-system]
requires = ["hatchling"]
build-backend = "hatchling.build"

[tool.hatch.build.targets.wheel]
packages = ["src/main/python/hydra"]
"""


def main() -> int:
    p = argparse.ArgumentParser(description=__doc__.splitlines()[0] if __doc__ else None)
    p.add_argument("package", help="Package name (e.g. hydra-kernel)")
    p.add_argument(
        "--repo-root",
        default=os.environ.get("HYDRA_ROOT_DIR"),
        help="Hydra worktree root (default: $HYDRA_ROOT_DIR)",
    )
    p.add_argument(
        "--out-dir",
        help="Override output directory (default: <repo-root>/dist/python/<package>)",
    )
    args = p.parse_args()

    if not args.repo_root:
        print("error: --repo-root or $HYDRA_ROOT_DIR is required", file=sys.stderr)
        return 2

    pkg_json_path = os.path.join(args.repo_root, "packages", args.package, "package.json")
    if not os.path.isfile(pkg_json_path):
        print(f"error: no such package.json: {pkg_json_path}", file=sys.stderr)
        return 1

    with open(pkg_json_path) as f:
        meta = json.load(f)

    pkg_name = meta.get("name") or args.package
    description = meta.get("description") or pkg_name
    deps = list(meta.get("dependencies") or [])

    version_path = os.path.join(args.repo_root, "VERSION")
    with open(version_path) as f:
        version = f.read().strip()

    out_dir = args.out_dir or os.path.join(args.repo_root, "dist", "python", args.package)
    os.makedirs(out_dir, exist_ok=True)

    # Reference packages/<pkg>/README.md only if it exists. Most kernel-set
    # packages don't have one; pyproject.toml's `readme` field requires the
    # file to exist if it's referenced.
    readme_src = os.path.join(args.repo_root, "packages", args.package, "README.md")
    readme_rel: str | None
    if os.path.isfile(readme_src):
        readme_rel = f"../../../packages/{args.package}/README.md"
    else:
        readme_rel = None

    pyproject_path = os.path.join(out_dir, "pyproject.toml")
    with open(pyproject_path, "w") as f:
        f.write(render_pyproject(pkg_name, description, version, deps, readme_rel))

    print(f"  wrote {pyproject_path}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
