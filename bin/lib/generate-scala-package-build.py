#!/usr/bin/env python3
"""Generate a standalone sbt build (build.sbt + project/) for one Hydra
Scala distribution package under dist/scala/<pkg>/.

Each emitted build is self-contained: from `dist/scala/<pkg>/`, running
`sbt package` produces a publishable jar.  `sbt publishSigned` (with
sbt-sonatype + sbt-pgp wired in via project/plugins.sbt) uploads to the
Sonatype Central Portal.

The emitted POM declares `net.fortytwo.hydra.scala:<pkg>_3:<version>` (Scala 3
cross-version suffix) with `compile`-scope dependencies on every Hydra
package listed in `packages/<pkg>/package.json`'s `dependencies` array.

Inputs:
  packages/<pkg>/package.json (read for name, description, dependencies)
  hydra.json                   (read for currentVersion)

Outputs:
  dist/scala/<pkg>/build.sbt
  dist/scala/<pkg>/project/plugins.sbt
  dist/scala/<pkg>/project/build.properties
"""

from __future__ import annotations

import argparse
import json
import os
import shutil
import sys


DEVELOPER_ID = "joshsh"
DEVELOPER_NAME = "Joshua Shinavier"
DEVELOPER_EMAIL = "josh@fortytwo.net"
LICENSE_NAME = "Apache-2.0"
HOMEPAGE = "https://github.com/CategoricalData/hydra"
SCM_CONNECTION = "scm:git@github.com:CategoricalData/hydra.git"
# #519: per-JVM-language Maven group (see generate-java-package-build.py). Scala
# artifacts publish under net.fortytwo.hydra.scala, keeping sbt's _3 suffix.
GROUP_ID = "net.fortytwo.hydra.scala"
SCALA3_VERSION = "3.3.7"
SBT_VERSION = "1.10.7"
# sbt-sonatype 3.12+ supports the new Central Portal endpoint.
SBT_SONATYPE_VERSION = "3.12.2"
SBT_PGP_VERSION = "2.3.1"

# External (non-Hydra) Scala dependencies per package.
# hydra-scala (the coder) uses commons-text via hydra-java on the Java side,
# but the Scala dist packages only carry generated Scala source — no runtime
# library deps beyond the cross-package Hydra deps declared in package.json.
EXTERNAL_DEPS: dict[str, list[str]] = {}


def render_build_sbt(name: str, description: str, version: str, deps: list[str],
                      bundle_dir: str) -> str:
    # Same escaping as `description` below: this is interpolated into a
    # double-quoted sbt string literal.
    bundle_dir = bundle_dir.replace("\\", "\\\\").replace('"', '\\"')
    dep_lines = []
    for dep in deps:
        # Hydra inter-package deps: the artifact names carry the _3 suffix
        # because they are also Scala 3 artifacts.
        dep_lines.append(
            f'    "{GROUP_ID}" % "{dep}_3" % "{version}"'
        )
    for ext in EXTERNAL_DEPS.get(name, []):
        dep_lines.append(f"    {ext}")

    if dep_lines:
        deps_block = ",\n".join(dep_lines)
        library_deps = f"  libraryDependencies ++= Seq(\n{deps_block}\n  ),\n"
    else:
        library_deps = "  // no inter-package Hydra dependencies\n"

    return f"""// Generated file. Do not edit.
// Regenerated by bin/lib/generate-scala-package-build.py from packages/{name}/package.json
// and hydra.json (currentVersion). Each dist/scala/<pkg>/ is a standalone sbt build:
// `sbt package` produces a jar; `sbt publishSigned` stages it into the SHARED
// bundle directory below. A single `sbt sonatypeBundleRelease` (run once,
// after every package's publishSigned) then uploads the combined bundle as
// ONE Central Portal deployment (#591) — see heads/scala/bin/publish-sbt.sh.

ThisBuild / organization := "{GROUP_ID}"
ThisBuild / version      := "{version}"
ThisBuild / scalaVersion := "{SCALA3_VERSION}"

// Sonatype Central Portal publishing (sbt-sonatype 3.12+).
// Credentials are read from ~/.sbt/1.0/sonatype.sbt or env vars:
//   SONATYPE_USERNAME / SONATYPE_PASSWORD
// Use the literal host string; sonatypeCentralHost is a plugin constant that
// is not in scope at ThisBuild evaluation time.
ThisBuild / sonatypeCredentialHost := "central.sonatype.com"
// `publishSigned` needs a target repository; sonatypePublishToBundle points it
// at the sbt-sonatype bundle staging dir, which sonatypeBundleRelease then
// submits to the Central Portal. Without this, publishSigned fails with
// "Repository for publishing is not specified."
ThisBuild / publishTo := sonatypePublishToBundle.value
// Declare the version scheme so downstream eviction tooling is satisfied.
ThisBuild / versionScheme := Some("early-semver")
publish / skip := false

lazy val root = (project in file("."))
  .settings(
    name        := "{name}",
    description := "{description}",
    licenses    += License.Apache2,
    homepage    := Some(url("{HOMEPAGE}")),
    scmInfo     := Some(ScmInfo(
      url("{HOMEPAGE}"),
      "{SCM_CONNECTION}"
    )),
    developers  := List(Developer(
      id    = "{DEVELOPER_ID}",
      name  = "{DEVELOPER_NAME}",
      email = "{DEVELOPER_EMAIL}",
      url   = url("https://fortytwo.net")
    )),

    // Source root is this package directory; sbt will find src/main/scala here.
    Compile / unmanagedSourceDirectories := Seq(baseDirectory.value / "src" / "main" / "scala"),

    // SHARED bundle directory (#591): every one of the 11 dist/scala/<pkg>/
    // builds points at this SAME absolute path (one directory ABOVE any
    // single package, so it survives regardless of build order), per
    // sbt-sonatype's documented multi-module aggregation mechanism —
    // publishSigned from each package only STAGES artifacts here; nothing is
    // uploaded until a single `sbt sonatypeCentralUpload`/`sonatypeBundleRelease`
    // runs once, afterward, against this directory.
    //
    // MUST be set at PROJECT scope (inside .settings(...), not as a bare
    // `ThisBuild / sonatypeBundleDirectory := ...`): sbt-sonatype's own
    // default is injected via AutoPlugin.projectSettings, which sbt resolves
    // at project scope — project scope is MORE specific than ThisBuild, so a
    // ThisBuild-scoped override is silently shadowed by the plugin's own
    // per-project default regardless of where it appears in this file
    // (confirmed empirically during #591 verification: `sonatypeBundleDirectory`
    // silently kept resolving to the plugin's target/sonatype-staging/<version>
    // default until moved into this settings block).
    sonatypeBundleDirectory := file("{bundle_dir}"),

    // The standalone per-package build is publish-focused; no test compilation.
    // Tests run from the developer rollup at packages/hydra-scala/.
    Test / unmanagedSourceDirectories := Seq.empty,
    test := {{}},

{library_deps}
    // Attach Scaladoc and sources jars for Maven Central.
    // packageDoc / publishArtifact is true by default in sbt with autoScalaLibrary,
    // but make it explicit so `sbt publishSigned` always bundles both.
    packageDoc / publishArtifact := true,
    packageSrc / publishArtifact := true,

    // Bundle LICENSE + NOTICE into META-INF of every jar.
    Compile / packageBin / mappings ++= Seq(
      (baseDirectory.value / "LICENSE") -> "META-INF/LICENSE",
      (baseDirectory.value / "NOTICE")  -> "META-INF/NOTICE"
    ),
    Compile / packageDoc / mappings ++= Seq(
      (baseDirectory.value / "LICENSE") -> "META-INF/LICENSE",
      (baseDirectory.value / "NOTICE")  -> "META-INF/NOTICE"
    ),
    Compile / packageSrc / mappings ++= Seq(
      (baseDirectory.value / "LICENSE") -> "META-INF/LICENSE",
      (baseDirectory.value / "NOTICE")  -> "META-INF/NOTICE"
    ),

    // publishSigned stages this package's artifacts into the shared
    // sonatypeBundleDirectory above; sonatypeBundleRelease (run once, across
    // all packages) uploads the combined bundle to the Central Portal.
  )
"""


def render_plugins_sbt() -> str:
    return f"""// Generated file. Do not edit.
// Publishing plugins for Sonatype Central Portal + GPG signing.
addSbtPlugin("org.xerial.sbt" % "sbt-sonatype" % "{SBT_SONATYPE_VERSION}")
addSbtPlugin("com.github.sbt" % "sbt-pgp"      % "{SBT_PGP_VERSION}")
"""


def render_build_properties() -> str:
    return f"sbt.version={SBT_VERSION}\n"


def main() -> int:
    p = argparse.ArgumentParser(description=__doc__.splitlines()[0] if __doc__ else None)
    p.add_argument("package", help="Package name (e.g. hydra-kernel)")
    p.add_argument(
        "--repo-root",
        default=os.environ.get("HYDRA_ROOT_DIR"),
        help="Hydra worktree root (default: $HYDRA_ROOT_DIR)",
    )
    p.add_argument(
        "--out-dir",
        help="Override output directory (default: <repo-root>/dist/scala/<package>)",
    )
    p.add_argument(
        "--bundle-dir",
        help="Shared sonatypeBundleDirectory for cross-package Central Portal "
             "aggregation (#591). Default: <repo-root>/dist/scala/.central-portal-bundle "
             "— the SAME path must be passed for every package in a publish run, "
             "so it is keyed off --repo-root, not --out-dir.",
    )
    args = p.parse_args()

    if not args.repo_root:
        print("error: --repo-root or $HYDRA_ROOT_DIR is required", file=sys.stderr)
        return 2

    pkg_json_path = os.path.join(args.repo_root, "packages", args.package, "package.json")
    if not os.path.isfile(pkg_json_path):
        print(f"error: no such package.json: {pkg_json_path}", file=sys.stderr)
        return 1

    with open(pkg_json_path) as f:
        meta = json.load(f)

    pkg_name = meta.get("name") or args.package
    description = meta.get("description") or pkg_name
    # Interpolated into a double-quoted sbt string: escape backslashes and
    # double quotes (same latent bug class as the java generator's
    # single-quote escape; apostrophes are safe here).
    description = description.replace("\\", "\\\\").replace('"', '\\"')
    deps = list(meta.get("dependencies") or [])

    with open(os.path.join(args.repo_root, "hydra.json")) as f:
        version = json.load(f)["currentVersion"]

    out_dir = args.out_dir or os.path.join(args.repo_root, "dist", "scala", args.package)
    project_dir = os.path.join(out_dir, "project")
    os.makedirs(out_dir, exist_ok=True)
    os.makedirs(project_dir, exist_ok=True)

    bundle_dir = args.bundle_dir or os.path.join(
        args.repo_root, "dist", "scala", ".central-portal-bundle")

    build_path = os.path.join(out_dir, "build.sbt")
    plugins_path = os.path.join(project_dir, "plugins.sbt")
    props_path = os.path.join(project_dir, "build.properties")

    with open(build_path, "w") as f:
        f.write(render_build_sbt(pkg_name, description, version, deps, bundle_dir))
    with open(plugins_path, "w") as f:
        f.write(render_plugins_sbt())
    with open(props_path, "w") as f:
        f.write(render_build_properties())

    # Copy LICENSE + NOTICE so the build.sbt mappings block can bundle them.
    for fname in ("LICENSE", "NOTICE"):
        shutil.copyfile(os.path.join(args.repo_root, fname), os.path.join(out_dir, fname))

    print(f"  wrote {build_path}")
    print(f"  wrote {plugins_path}")
    print(f"  wrote {props_path}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
