module_ :: Module
module_ = Module (Namespace "hydra.ext.org.json.schema.language")
  [el jsonSchemaLanguageDef]
  [Variants.module_]
  KernelTypes.kernelTypesModules $
  Just "Language constraints for JSON Schema"

define :: String -> TTerm a -> TBinding a
define = definitionInModule module_

jsonSchemaLanguageDef :: TBinding Language
jsonSchemaLanguageDef = define "jsonSchemaLanguage" $
  doc "Language constraints for JSON Schema" $ lets [
  "eliminationVariants">: Sets.fromList $ list [],
  "literalVariants">: Sets.fromList $ list [
    Meta.literalVariantBoolean,
    Meta.literalVariantFloat,
    Meta.literalVariantInteger,
    Meta.literalVariantString],
  "floatTypes">: Sets.fromList $ list [Core.floatTypeBigfloat],
  "functionVariants">: Sets.fromList $ list [],
  "integerTypes">: Sets.fromList $ list [Core.integerTypeBigint],
  "termVariants">: Sets.fromList $ ref Variants.termVariantsDef,
  "typeVariants">: Sets.fromList $ list [
    Meta.typeVariantAnnotated,
    Meta.typeVariantList,
    Meta.typeVariantLiteral,
    Meta.typeVariantMap,
    Meta.typeVariantMaybe,
    Meta.typeVariantRecord,
    Meta.typeVariantUnion,
    Meta.typeVariantVariable],
  "typePredicate">: constant true] $
  Coders.language
    (Coders.languageName $ string "hydra.ext.org.json.schema")
    (Coders.languageConstraints
      (var "eliminationVariants")
      (var "literalVariants")
      (var "floatTypes")
      (var "functionVariants")
      (var "integerTypes")
      (var "termVariants")
      (var "typeVariants")
      (var "typePredicate"))
