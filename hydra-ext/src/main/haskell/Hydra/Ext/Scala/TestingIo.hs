-- | Convert a Hydra term to a Scala expression string
termToScala :: Term -> Graph -> Either String String
termToScala term g = case encodeTerm emptyContext g term of
    Left ic -> Left $ ShowError.error (inContextObject ic)
    Right scalaData -> Right $ printExpr $ parenthesize $ writeTerm scalaData

-- | Convert a Hydra type to a Scala type expression string
typeToScala :: Type -> Graph -> Either String String
typeToScala _ _ = Right "Any"

-- | Create a Scala TestCodec
scalaTestCodec :: TestCodec
scalaTestCodec = TestCodec {
    testCodecLanguage = LanguageName "scala",
    testCodecFileExtension = FileExtension "scala",
    testCodecEncodeTerm = termToScala,
    testCodecEncodeType = typeToScala,
    testCodecFormatTestName = formatScalaTestName,
    testCodecFormatModuleName = namespaceToScalaClassName,
    testCodecTestCaseTemplate = scalaTestCaseTemplate,
    testCodecTestGroupTemplate = scalaTestGroupTemplate,
    testCodecModuleTemplate = scalaModuleTemplate,
    testCodecImportTemplate = scalaImportTemplate,
    testCodecFindImports = \_ -> findScalaImports}

-- | Format a test name for Scala (camelCase method name)
-- Preserves numbers and handles special characters to maintain uniqueness
formatScalaTestName :: String -> String
formatScalaTestName name = "test" ++ toCamelCase (preprocessName name)
  where
    -- Preprocess: convert special chars to words to preserve uniqueness
    preprocessName s = concatMap charToWord s
    charToWord c
      | c == '-' = " Neg"  -- Negative sign
      | c == '.' = "Dot"   -- Decimal point
      | c == '+' = " Plus"
      | c == '/' = " Div"
      | c == '*' = " Mul"
      | c == '#' = " Num"
      | otherwise = [c]
    toCamelCase s = concatMap capitalize $ words $ map toSnake s
    toSnake c
      | isAlphaNum c = c
      | otherwise = ' '
    capitalize [] = []
    capitalize (c:cs) = toUpper c : map toLower cs

