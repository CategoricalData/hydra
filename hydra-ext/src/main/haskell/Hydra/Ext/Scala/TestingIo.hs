-- | Convert namespace to Scala class name
namespaceToScalaClassName :: Namespace -> String
namespaceToScalaClassName (Namespace ns) =
  Strings.intercalate "." $ Lists.map Formatting.capitalize (Strings.splitOn "." ns)

-- | Generate test hierarchy for Scala
generateScalaTestGroupHierarchy :: Graph -> TestCodec -> [String] -> TestGroup -> Either String String
generateScalaTestGroupHierarchy g codec groupPath testGroup = do
  -- Generate test cases at the current level
  testCaseLinesRaw <- mapM (generateScalaTestCase g codec groupPath) (testGroupCases testGroup)
  let testCaseLines = concat testCaseLinesRaw
      testCasesStr = L.intercalate "\n\n" testCaseLines

  -- Generate sections for each subgroup, passing the subgroup name in the path
  subgroupStrs <- mapM generateSubgroupBlock (testGroupSubgroups testGroup)
  let subgroupsStr = L.intercalate "\n\n" subgroupStrs

  -- Combine test cases and subgroups
  return $ testCasesStr ++ (if null testCasesStr || null subgroupsStr then "" else "\n\n") ++ subgroupsStr
  where
    generateSubgroupBlock :: TestGroup -> Either String String
    generateSubgroupBlock subgroup = do
      let groupName = testGroupName subgroup
      subgroupContent <- generateScalaTestGroupHierarchy g codec (groupPath ++ [groupName]) subgroup
      let header = "  // " ++ groupName
      return $ header ++ "\n\n" ++ subgroupContent

-- | Assertion type for Scala tests
data AssertionType
  = AssertEquals           -- Standard assert(x == y)
  | AssertBigDecimalEquals -- For BigDecimal comparison using compare
  | AssertDoubleEquals     -- For double comparison with tolerance
  deriving (Eq, Show)

-- | Determine the assertion type based on the output term structure
getAssertionType :: Term -> AssertionType
getAssertionType term = case stripAnnotations term of
  TermLiteral (LiteralFloat fv) -> case fv of
    FloatValueBigfloat _ -> AssertBigDecimalEquals
    FloatValueFloat64 _ -> AssertDoubleEquals
    FloatValueFloat32 _ -> AssertDoubleEquals
  _ -> AssertEquals
  where
    stripAnnotations (TermAnnotated (AnnotatedTerm t _)) = stripAnnotations t
    stripAnnotations t = t

-- | Generate the assertion code based on the assertion type
generateAssertion :: AssertionType -> String -> String -> [String]
generateAssertion assertType outputCode inputCode = case assertType of
  AssertEquals ->
    ["  test(\"{name}\") {",
     "    assert((",
     "      " ++ inputCode ++ ") == (",
     "      " ++ outputCode ++ "))",
     "  }"]
  AssertBigDecimalEquals ->
    ["  test(\"{name}\") {",
     "    assert((" ++ outputCode ++ ").compare(" ++ inputCode ++ ") == 0)",
     "  }"]
  AssertDoubleEquals ->
    ["  test(\"{name}\") {",
     "    assert(math.abs((" ++ inputCode ++ ") - (" ++ outputCode ++ ")) <= 1e-15)",
     "  }"]

-- | Generate a single test case for Scala/ScalaTest
generateScalaTestCase :: Graph -> TestCodec -> [String] -> TestCaseWithMetadata -> Either String [String]
generateScalaTestCase g codec groupPath (TestCaseWithMetadata name tcase _ _) = case tcase of
  TestCaseDelegatedEvaluation (DelegatedEvaluationTestCase input output) -> do
    inputCode <- testCodecEncodeTerm codec input g
    outputCode <- testCodecEncodeTerm codec output g

    -- Include group path in test name to avoid collisions
    let fullName = if L.null groupPath
          then name
          else L.intercalate " - " (groupPath ++ [name])
        assertType = getAssertionType output
    case assertType of
      AssertEquals ->
        return [
          "  test(\"" ++ escapeScalaString fullName ++ "\") {",
          "    assert((",
          "      " ++ inputCode ++ ") == (",
          "      " ++ outputCode ++ "))",
          "  }"]
      AssertBigDecimalEquals ->
        return [
          "  test(\"" ++ escapeScalaString fullName ++ "\") {",
          "    assert((" ++ outputCode ++ ").compare(" ++ inputCode ++ ") == 0)",
          "  }"]
      AssertDoubleEquals ->
        return [
          "  test(\"" ++ escapeScalaString fullName ++ "\") {",
          "    assert(math.abs((" ++ inputCode ++ ") - (" ++ outputCode ++ ")) <= 1e-15)",
          "  }"]

  _ -> return []  -- Skip non-delegated tests

-- | Escape special characters in a Scala string literal
escapeScalaString :: String -> String
escapeScalaString = concatMap escChar
  where
    escChar '"'  = "\\\""
    escChar '\\' = "\\\\"
    escChar '\n' = "\\n"
    escChar c    = [c]

-- | Generate test file using Scala codec
generateTestFileWithScalaCodec :: TestCodec -> Module -> TestGroup -> Graph -> Either String (FilePath, String)
generateTestFileWithScalaCodec codec testModule testGroup g = do
  -- Generate test hierarchy
  testBody <- generateScalaTestGroupHierarchy g codec [] testGroup

  -- Build the complete test module
  let testModuleContent = buildScalaTestModule codec testModule testGroup testBody

  -- Generate file path: namespace -> path
  -- Strip "generation." prefix to avoid double "generation/generation/..."
  let Namespace ns = moduleNamespace testModule
      parts = Strings.splitOn "." ns
      dirParts = drop 1 (L.init parts)  -- drop "generation" prefix
      -- Add "Test" suffix to class name
      className = Formatting.capitalize (L.last parts) ++ "Test"
      fileName = className ++ ".scala"
      filePath = L.intercalate "/" dirParts ++ "/" ++ fileName

  return (filePath, testModuleContent)

-- | Build complete Scala test module
buildScalaTestModule :: TestCodec -> Module -> TestGroup -> String -> String
buildScalaTestModule codec testModule testGroup testBody = header ++ testBody ++ "\n}\n"
  where
    Namespace ns = moduleNamespace testModule
    parts = Strings.splitOn "." ns
    packageName = L.intercalate "." (L.init parts)
    className = Formatting.capitalize (L.last parts) ++ "Test"
    groupName = testGroupName testGroup

    -- Standard imports for Scala tests
    standardImports = [
      "import org.scalatest.funsuite.AnyFunSuite"]

    header = unlines [
        "// " ++ warningAutoGeneratedFile,
        "// " ++ groupName,
        "",
        "package " ++ packageName,
        ""
      ] ++ L.intercalate "\n" standardImports ++ "\n\n" ++
      "class " ++ className ++ " extends AnyFunSuite {\n\n"

-- | Generate Scala test file for a test group
generateScalaTestFile :: Module -> TestGroup -> Graph -> Either String (FilePath, String)
generateScalaTestFile testModule testGroup g = do
  -- Run type inference on all test terms to ensure lambdas have domain types
  inferredTestGroup <- inferTestGroupTerms g testGroup

  -- Generate test file using the codec
  generateTestFileWithScalaCodec scalaTestCodec testModule inferredTestGroup g

-- | Run type inference on all terms in a TestGroup
inferTestGroupTerms :: Graph -> TestGroup -> Either String TestGroup
inferTestGroupTerms g (TestGroup name desc subgroups cases) = do
  inferredSubgroups <- mapM (inferTestGroupTerms g) subgroups
  inferredCases <- mapM (inferTestCase g) cases
  return $ TestGroup name desc inferredSubgroups inferredCases

-- | Run type inference on the terms in a test case
inferTestCase :: Graph -> TestCaseWithMetadata -> Either String TestCaseWithMetadata
inferTestCase g (TestCaseWithMetadata name tcase desc tags) = do
  inferredTcase <- case tcase of
    TestCaseDelegatedEvaluation (DelegatedEvaluationTestCase input output) -> do
      inferredInput <- inferTerm g input
      inferredOutput <- inferTerm g output
      return $ TestCaseDelegatedEvaluation $ DelegatedEvaluationTestCase inferredInput inferredOutput
    other -> return other
  return $ TestCaseWithMetadata name inferredTcase desc tags

-- | Run type inference on a single term
inferTerm :: Graph -> Term -> Either String Term
inferTerm g term = case Inference.inferInGraphContext emptyContext g term of
    Left ic -> Left (ShowError.error (inContextObject ic))
    Right result -> Right (inferenceResultTerm result)

-- | Scala-specific test generator
scalaTestGenerator :: TestGenerator ()
scalaTestGenerator = TestGenerator {
  testGeneratorNamespacesForModule = \_ _ -> Right $ Namespaces (Namespace "", ()) M.empty,
  testGeneratorCreateCodec = \_ -> scalaTestCodec,
  testGeneratorGenerateTestFile = generateScalaTestFile,
  testGeneratorAggregatorFile = Nothing
}

-- | Main entry point for generating Scala generation tests
generateScalaGenerationTests :: FilePath -> [Module] -> (Namespace -> Maybe TestGroup) -> IO Bool
generateScalaGenerationTests = generateGenerationTestSuite scalaTestGenerator
