#!/bin/bash
# Bootstrap Hydra to Haskell from JSON modules (via Python host).
# Generates code into a standalone /tmp directory, copies static resources,
# then builds and runs the Haskell test suite.
#
# Usage: ./python-to-haskell.sh [--types-only] [--kernel-only]

set -e

SCRIPT_DIR="$( cd "$( dirname "${BASH_SOURCE[0]}" )" && pwd )"
HYDRA_ROOT="$( cd "$SCRIPT_DIR/../../.." && pwd )"
HYDRA_HASKELL_DIR="$HYDRA_ROOT/hydra-haskell"
HASKELL_RESOURCES="$SCRIPT_DIR/resources/haskell"

OUTPUT_BASE="/tmp/hydra-bootstrapping-demo"
OUTPUT_DIR="$OUTPUT_BASE/python-to-haskell"

# Collect extra flags
EXTRA_FLAGS=""
for arg in "$@"; do
    case "$arg" in
        --types-only|--kernel-only) EXTRA_FLAGS="$EXTRA_FLAGS $arg" ;;
    esac
done

echo "=========================================="
echo "Bootstrap: Python -> JSON -> Haskell"
echo "=========================================="
echo ""
echo "  Host language:   Python"
echo "  Target language: Haskell"
echo "  Output:          $OUTPUT_DIR"
if [ -n "$EXTRA_FLAGS" ]; then
    echo "  Flags:          $EXTRA_FLAGS"
fi
echo ""

TOTAL_START=$(date +%s)

# Step 1: Verify JSON modules exist
echo "Step 1: Verifying JSON modules..."
JSON_DIR="$HYDRA_HASKELL_DIR/src/gen-main/json"
JSON_COUNT=$(find "$JSON_DIR" -name "*.json" 2>/dev/null | wc -l | tr -d ' ')
if [ "$JSON_COUNT" -eq 0 ]; then
    echo "  ERROR: No JSON files found in $JSON_DIR"
    echo "  Ensure the repository is synced. See bin/sync-haskell.sh."
    exit 1
fi
echo "  JSON input files: $JSON_COUNT (in $JSON_DIR)"
echo ""

# Step 2: Clean output directory
echo "Step 2: Preparing output directory..."
rm -rf "$OUTPUT_DIR"
mkdir -p "$OUTPUT_DIR"
echo "  Cleaned: $OUTPUT_DIR"
echo ""

# Step 3: Bootstrap from JSON via Python host
echo "Step 3: Generating Haskell code from JSON (via Python host)..."
STEP_START=$(date +%s)
"$SCRIPT_DIR/python-bootstrap.sh" --target haskell --output "$OUTPUT_BASE" $EXTRA_FLAGS 2>&1
STEP_END=$(date +%s)
echo "  Time: $((STEP_END - STEP_START))s"
echo ""

# Step 4: Copy static resources
echo "Step 4: Copying static resources..."

# Build files
echo "  Copying build files..."
cp "$HASKELL_RESOURCES/stack.yaml" "$OUTPUT_DIR/"
cp "$HASKELL_RESOURCES/package.yaml" "$OUTPUT_DIR/"
cp "$HASKELL_RESOURCES/README.md" "$OUTPUT_DIR/"

# Hand-written source files (primitive libraries, DSL, sources, etc.)
echo "  Copying hand-written source files..."
mkdir -p "$OUTPUT_DIR/src/main/haskell"
cp -r "$HYDRA_HASKELL_DIR/src/main/haskell/Hydra" "$OUTPUT_DIR/src/main/haskell/"

# Kernel sources modules (Hydra.Sources.*) needed for evaluation tests.
# These are generated by update-haskell-sources, not from JSON, so they must
# be copied as static resources.
echo "  Copying kernel sources modules..."
if [ -d "$HYDRA_HASKELL_DIR/src/gen-main/haskell/Hydra/Sources" ]; then
    mkdir -p "$OUTPUT_DIR/src/gen-main/haskell/Hydra/Sources"
    cp -r "$HYDRA_HASKELL_DIR/src/gen-main/haskell/Hydra/Sources/"* "$OUTPUT_DIR/src/gen-main/haskell/Hydra/Sources/"
fi

# Test infrastructure
echo "  Copying test infrastructure..."
mkdir -p "$OUTPUT_DIR/src/test/haskell"
cp "$HYDRA_HASKELL_DIR/src/test/haskell/Spec.hs" "$OUTPUT_DIR/src/test/haskell/"
cp "$HYDRA_HASKELL_DIR/src/test/haskell/GenerationSpec.hs" "$OUTPUT_DIR/src/test/haskell/"

# Generation test specs (generated by update-generation-tests, not from JSON)
echo "  Copying generation test specs..."
if [ -d "$HYDRA_HASKELL_DIR/src/gen-test/haskell/Generation" ]; then
    mkdir -p "$OUTPUT_DIR/src/gen-test/haskell/"
    cp -r "$HYDRA_HASKELL_DIR/src/gen-test/haskell/Generation" "$OUTPUT_DIR/src/gen-test/haskell/"
fi

# License (needed by cabal)
touch "$OUTPUT_DIR/LICENSE"
echo ""

# Summary
echo "Step 5: Summary of generated files..."
MAIN_COUNT=$(find "$OUTPUT_DIR/src/gen-main" -name "*.hs" 2>/dev/null | wc -l | tr -d ' ')
TEST_COUNT=$(find "$OUTPUT_DIR/src/gen-test" -name "*.hs" 2>/dev/null | wc -l | tr -d ' ')
STATIC_COUNT=$(find "$OUTPUT_DIR/src/main" -name "*.hs" 2>/dev/null | wc -l | tr -d ' ')
echo "  Generated main modules:   $MAIN_COUNT files"
echo "  Generated test modules:   $TEST_COUNT files"
echo "  Static resources:         $STATIC_COUNT files"
echo ""

echo ""

# Step 6: Run tests
echo "Step 6: Running tests..."
STEP_START=$(date +%s)
cd "$OUTPUT_DIR"
stack test 2>&1
TEST_EXIT=$?
STEP_END=$(date +%s)
echo "  Test time: $((STEP_END - STEP_START))s"
echo ""

TOTAL_END=$(date +%s)
TOTAL_SECS=$((TOTAL_END - TOTAL_START))

echo "=========================================="
if [ $TEST_EXIT -eq 0 ]; then
    echo "Bootstrap Python-to-Haskell: COMPLETE (tests PASSED)"
else
    echo "Bootstrap Python-to-Haskell: COMPLETE (tests FAILED)"
fi
echo "  Output:     $OUTPUT_DIR"
echo "  Total time: ${TOTAL_SECS}s"
echo "=========================================="

exit $TEST_EXIT
