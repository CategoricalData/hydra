#!/bin/bash
# Bootstrap Hydra to Python from JSON modules (via Java host).
# Generates code into a standalone /tmp directory, copies static resources,
# then runs the Python test suite.
#
# Usage: ./java-to-python.sh [--types-only] [--kernel-only]

set -e

SCRIPT_DIR="$( cd "$( dirname "${BASH_SOURCE[0]}" )" && pwd )"
HYDRA_ROOT="$( cd "$SCRIPT_DIR/../../.." && pwd )"
HYDRA_EXT_DIR="$HYDRA_ROOT/hydra-ext"
HYDRA_HASKELL_DIR="$HYDRA_ROOT/hydra-haskell"
HYDRA_PYTHON_DIR="$HYDRA_ROOT/hydra-python"

OUTPUT_BASE="/tmp/hydra-bootstrapping-demo"
OUTPUT_DIR="$OUTPUT_BASE/java-to-python"

# Collect extra flags
EXTRA_FLAGS=""
for arg in "$@"; do
    case "$arg" in
        --types-only|--kernel-only) EXTRA_FLAGS="$EXTRA_FLAGS $arg" ;;
    esac
done

echo "=========================================="
echo "Bootstrap: Java -> JSON -> Python"
echo "=========================================="
echo ""
echo "  Host language:   Java"
echo "  Target language: Python"
echo "  Output:          $OUTPUT_DIR"
if [ -n "$EXTRA_FLAGS" ]; then
    echo "  Flags:          $EXTRA_FLAGS"
fi
echo ""

TOTAL_START=$(date +%s)

# Step 1: Ensure JSON is up to date
echo "Step 1: Ensuring JSON exports are up to date..."
STEP_START=$(date +%s)
echo "  Building hydra-haskell..."
cd "$HYDRA_HASKELL_DIR"
stack build 2>&1 | tail -3
echo "  Exporting main modules to JSON..."
stack exec update-json-main -- +RTS -K256M -A32M -RTS
echo "  Exporting test modules to JSON..."
stack exec update-json-test -- +RTS -K256M -A32M -RTS
echo "  Building hydra-ext..."
cd "$HYDRA_EXT_DIR"
stack build 2>&1 | tail -3
echo "  Exporting ext modules to JSON..."
stack exec update-json-main -- +RTS -K256M -A32M -RTS
STEP_END=$(date +%s)
echo "  Time: $((STEP_END - STEP_START))s"
echo ""

# Count JSON input files
JSON_DIR="$HYDRA_HASKELL_DIR/src/gen-main/json"
JSON_COUNT=$(find "$JSON_DIR" -name "*.json" 2>/dev/null | wc -l | tr -d ' ')
echo "  JSON input files: $JSON_COUNT (in $JSON_DIR)"
echo ""

# Step 2: Clean output directory
echo "Step 2: Preparing output directory..."
rm -rf "$OUTPUT_DIR"
mkdir -p "$OUTPUT_DIR"
echo "  Cleaned: $OUTPUT_DIR"
echo ""

# Step 3: Bootstrap from JSON via Java host
echo "Step 3: Generating Python code from JSON (via Java host)..."
STEP_START=$(date +%s)
"$SCRIPT_DIR/java-bootstrap.sh" --target python --output "$OUTPUT_BASE" $EXTRA_FLAGS 2>&1
STEP_END=$(date +%s)
echo "  Time: $((STEP_END - STEP_START))s"
echo ""

# Create namespace package __init__.py files in generated directories.
# Python needs these to merge the hydra package across src/main, src/gen-main,
# and src/gen-test directories using pkgutil.extend_path.
echo "  Creating namespace package __init__.py files..."
for gen_dir in "$OUTPUT_DIR/src/gen-main/python" "$OUTPUT_DIR/src/gen-test/python"; do
    if [ -d "$gen_dir" ]; then
        find "$gen_dir" -type d -not -name "__pycache__" -not -path "*/__pycache__/*" | while read dir; do
            # Skip the top-level python/ directory itself
            if [ "$dir" = "$gen_dir" ]; then continue; fi
            if [ ! -f "$dir/__init__.py" ]; then
                echo "from pkgutil import extend_path" > "$dir/__init__.py"
                echo "__path__ = extend_path(__path__, __name__)" >> "$dir/__init__.py"
            fi
        done
    fi
done
echo ""

# Step 4: Copy static resources
echo "Step 4: Copying static resources..."

# Build/config files
echo "  Copying build configuration..."
cp "$HYDRA_PYTHON_DIR/pyproject.toml" "$OUTPUT_DIR/"
cp "$HYDRA_PYTHON_DIR/pyrightconfig.json" "$OUTPUT_DIR/" 2>/dev/null || true
cp "$HYDRA_PYTHON_DIR/uv.lock" "$OUTPUT_DIR/" 2>/dev/null || true

# README.md (needed by hatchling build)
touch "$OUTPUT_DIR/README.md"

# Hand-written source files: explicit inventory of what the Hydra
# implementation needs beyond generated code.
echo "  Copying hand-written source files..."
PY_SRC="$HYDRA_PYTHON_DIR/src/main/python/hydra"
PY_DST="$OUTPUT_DIR/src/main/python/hydra"
mkdir -p "$PY_DST"

# Top-level files (excludes bootstrap.py and generation.py which are
# host-specific and not needed in the standalone bootstrap)
for f in __init__.py tools.py py.typed; do
    cp "$PY_SRC/$f" "$PY_DST/" 2>/dev/null || true
done

# Directories copied wholesale (excluding __pycache__)
for d in lib dsl sources; do
    if [ -d "$PY_SRC/$d" ]; then
        cp -r "$PY_SRC/$d" "$PY_DST/"
        find "$PY_DST/$d" -name "__pycache__" -type d -exec rm -rf {} + 2>/dev/null || true
    fi
done

# Kernel sources modules (hydra.sources.*) needed for evaluation tests.
# These are generated by update-python-sources, not from JSON, so they must
# be copied as static resources.
echo "  Copying kernel sources modules..."
if [ -d "$HYDRA_PYTHON_DIR/src/gen-main/python/hydra/sources" ]; then
    mkdir -p "$OUTPUT_DIR/src/gen-main/python/hydra/sources"
    cp -r "$HYDRA_PYTHON_DIR/src/gen-main/python/hydra/sources/"* "$OUTPUT_DIR/src/gen-main/python/hydra/sources/"
    find "$OUTPUT_DIR/src/gen-main/python/hydra/sources" -name "__pycache__" -type d -exec rm -rf {} + 2>/dev/null || true
fi

# Test infrastructure
echo "  Copying test infrastructure..."
mkdir -p "$OUTPUT_DIR/src/test/python"
cp "$HYDRA_PYTHON_DIR/src/test/python/conftest.py" "$OUTPUT_DIR/src/test/python/" 2>/dev/null || true
for f in test_suite_runner.py test_python.py test_generated_code.py test_grammar.py test_json.py test_lazy_flow_evaluation.py kernel_bindings.py test_summary_report.py; do
  if [ -f "$HYDRA_PYTHON_DIR/src/test/python/$f" ]; then
    cp "$HYDRA_PYTHON_DIR/src/test/python/$f" "$OUTPUT_DIR/src/test/python/"
  fi
done
echo ""

# Summary
echo "Step 5: Summary of generated files..."
MAIN_COUNT=$(find "$OUTPUT_DIR/src/gen-main" -name "*.py" 2>/dev/null | wc -l | tr -d ' ')
TEST_COUNT=$(find "$OUTPUT_DIR/src/gen-test" -name "*.py" 2>/dev/null | wc -l | tr -d ' ')
STATIC_COUNT=$(find "$OUTPUT_DIR/src/main" -name "*.py" 2>/dev/null | wc -l | tr -d ' ')
echo "  Generated main modules:   $MAIN_COUNT files"
echo "  Generated test modules:   $TEST_COUNT files"
echo "  Static resources:         $STATIC_COUNT files"
echo ""

echo ""

# Step 6: Run tests
echo "Step 6: Running tests..."
STEP_START=$(date +%s)
cd "$OUTPUT_DIR"
pytest src/test/ src/gen-test/ 2>&1
TEST_EXIT=$?
STEP_END=$(date +%s)
echo "  Test time: $((STEP_END - STEP_START))s"
echo ""

TOTAL_END=$(date +%s)
TOTAL_SECS=$((TOTAL_END - TOTAL_START))

echo "=========================================="
if [ $TEST_EXIT -eq 0 ]; then
    echo "Bootstrap Java-to-Python: COMPLETE (tests PASSED)"
else
    echo "Bootstrap Java-to-Python: COMPLETE (tests FAILED)"
fi
echo "  Output:     $OUTPUT_DIR"
echo "  Total time: ${TOTAL_SECS}s"
echo "=========================================="

exit $TEST_EXIT
