#!/bin/bash
# Copy static resources and run tests for a Haskell bootstrap target directory.
# This is host-language-independent: the same static resources are needed
# regardless of which host (Haskell, Java, Python) generated the code.
#
# Usage: ./setup-haskell-target.sh <output-dir>
#
# The output directory should already contain src/gen-main/haskell/ with generated code.

set -e

OUTPUT_DIR="$1"
if [ -z "$OUTPUT_DIR" ]; then
    echo "Usage: $0 <output-dir>"
    exit 1
fi

SCRIPT_DIR="$( cd "$( dirname "${BASH_SOURCE[0]}" )" && pwd )"
HYDRA_ROOT="$( cd "$SCRIPT_DIR/../../.." && pwd )"
HYDRA_HASKELL_DIR="$HYDRA_ROOT/hydra-haskell"
HASKELL_RESOURCES="$SCRIPT_DIR/resources/haskell"

# Step: Copy static resources
echo "Copying static resources for Haskell target..."

# Build files
echo "  Copying build files..."
cp "$HASKELL_RESOURCES/stack.yaml" "$OUTPUT_DIR/"
cp "$HASKELL_RESOURCES/package.yaml" "$OUTPUT_DIR/"
cp "$HASKELL_RESOURCES/README.md" "$OUTPUT_DIR/"

# Hand-written source files (primitive libraries, DSL, sources, etc.)
echo "  Copying hand-written source files..."
mkdir -p "$OUTPUT_DIR/src/main/haskell"
cp -r "$HYDRA_HASKELL_DIR/src/main/haskell/Hydra" "$OUTPUT_DIR/src/main/haskell/"

# Kernel sources modules (Hydra.Sources.*) needed for evaluation tests.
echo "  Copying kernel sources modules..."
if [ -d "$HYDRA_HASKELL_DIR/src/gen-main/haskell/Hydra/Sources" ]; then
    mkdir -p "$OUTPUT_DIR/src/gen-main/haskell/Hydra/Sources"
    cp -r "$HYDRA_HASKELL_DIR/src/gen-main/haskell/Hydra/Sources/"* "$OUTPUT_DIR/src/gen-main/haskell/Hydra/Sources/"
fi

# Test infrastructure
echo "  Copying test infrastructure..."
mkdir -p "$OUTPUT_DIR/src/test/haskell"
cp "$HYDRA_HASKELL_DIR/src/test/haskell/Spec.hs" "$OUTPUT_DIR/src/test/haskell/"
cp "$HYDRA_HASKELL_DIR/src/test/haskell/GenerationSpec.hs" "$OUTPUT_DIR/src/test/haskell/"

# Generation test specs (generated by update-generation-tests, not from JSON)
echo "  Copying generation test specs..."
if [ -d "$HYDRA_HASKELL_DIR/src/gen-test/haskell/Generation" ]; then
    mkdir -p "$OUTPUT_DIR/src/gen-test/haskell"
    cp -r "$HYDRA_HASKELL_DIR/src/gen-test/haskell/Generation" "$OUTPUT_DIR/src/gen-test/haskell/"
fi

# License (needed by cabal)
touch "$OUTPUT_DIR/LICENSE"

# Summary
MAIN_COUNT=$(find "$OUTPUT_DIR/src/gen-main" -name "*.hs" 2>/dev/null | wc -l | tr -d ' ')
TEST_COUNT=$(find "$OUTPUT_DIR/src/gen-test" -name "*.hs" 2>/dev/null | wc -l | tr -d ' ')
STATIC_COUNT=$(find "$OUTPUT_DIR/src/main" -name "*.hs" 2>/dev/null | wc -l | tr -d ' ')
echo "  Generated main modules:   $MAIN_COUNT files"
echo "  Generated test modules:   $TEST_COUNT files"
echo "  Static resources:         $STATIC_COUNT files"
echo ""

# Run tests
echo "Running Haskell tests..."
STEP_START=$(date +%s)
cd "$OUTPUT_DIR"
stack test 2>&1
TEST_EXIT=$?
STEP_END=$(date +%s)
echo "  Test time: $((STEP_END - STEP_START))s"
echo ""

exit $TEST_EXIT
