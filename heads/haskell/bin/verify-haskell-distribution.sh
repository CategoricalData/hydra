#!/usr/bin/env bash
# Verify the per-package Hydra Haskell distributions COMPILE — both standalone
# (hydra-kernel, which has no Hydra deps) and together (hydra-haskell, hydra,
# which depend on hydra-kernel == <version>, not yet on Hackage).
#
# Because the dependents pin a not-yet-published hydra-kernel, we can't `stack
# build` them in isolation. Instead this stages all three packages and wires
# them into ONE multi-package stack project (local `packages:` entries), so the
# pinned inter-package deps resolve against the local staged copies. A clean
# `stack build` of that project proves the trio compiles as published.
#
# This is verification only — it does NOT produce or upload sdists (see
# publish-hackage.sh). It is the Haskell analog of running each head's
# test-distribution.sh against the assembled dist.
#
# Usage:
#   verify-haskell-distribution.sh [--keep]
#     --keep  leave the staging/build dir in place for inspection.

set -euo pipefail

SCRIPT_DIR="$( cd "$( dirname "${BASH_SOURCE[0]}" )" && pwd )"
HYDRA_HASKELL_DIR="$( cd "$SCRIPT_DIR/.." && pwd )"
HYDRA_ROOT="$( cd "$HYDRA_HASKELL_DIR/../.." && pwd )"

KEEP=false
[ "${1:-}" = "--keep" ] && KEEP=true

VERSION="$(cat "$HYDRA_ROOT/VERSION")"
PKGS=(hydra-kernel hydra-haskell hydra)

WORK="$(mktemp -d -t hydra-verify-dist-XXXXXX)"
cleanup() { [ "$KEEP" = true ] || rm -rf "$WORK"; }
trap cleanup EXIT

echo "=== Verifying Hydra Haskell per-package distributions compile ==="
echo "  Work dir: $WORK"
echo ""

# Stage each package into $WORK/<pkg> (no sdist), reusing the assembler's
# --no-sdist mode but redirecting its temp staging into our controlled tree.
# The assembler stages into its own mktemp dir, so we capture and move it.
for pkg in "${PKGS[@]}"; do
    echo "--- staging $pkg ---"
    out="$("$SCRIPT_DIR/assemble-haskell-distribution.sh" "$pkg" --no-sdist 2>&1)"
    echo "$out" | grep -E 'staged'
    stage="$(echo "$out" | grep -oE '/[^ ]+hydra-sdist-'"$pkg"'-[^ ]+' | head -1)"
    if [ -z "$stage" ] || [ ! -d "$stage" ]; then
        echo "ERROR: could not locate staging dir for $pkg" >&2
        exit 1
    fi
    mkdir -p "$WORK/$pkg"
    cp -R "$stage"/. "$WORK/$pkg/"
    # Drop the per-package stack.yaml; the umbrella project below supplies one.
    rm -f "$WORK/$pkg/stack.yaml"
    rm -rf "$stage"
done
echo ""

# One multi-package stack project tying the three local packages together so the
# `== <version>` inter-package pins resolve against the local staged copies.
cat > "$WORK/stack.yaml" <<YAML
# Generated by verify-haskell-distribution.sh — ephemeral verification project.
resolver: lts-24.7
system-ghc: true
install-ghc: false
packages:
  - hydra-kernel
  - hydra-haskell
  - hydra
extra-deps: []
YAML

echo "=== Building the trio together (stack build) ==="
cd "$WORK"
export STACK_WORK_DIR="$WORK/.stack-work"
if stack build 2>&1 | tee "$WORK/verify-build.log"; then
    echo ""
    echo "=== OK: all three packages compile as published ==="
else
    echo ""
    echo "=== FAIL: trio did not compile (see $WORK/verify-build.log) ===" >&2
    KEEP=true   # keep for debugging
    exit 1
fi
