#!/bin/bash
# Generate alist-compatible accessor functions for all cl:defstruct definitions
# in the generated Common Lisp code.
#
# The generated code constructs records as alists '((:key . val) ...)
# but accesses them via struct-style accessors (type_name-field rec).
# This script generates a compatibility file that defines those accessors
# to work on alists.

GENDIR="$(cd "$(dirname "$0")/../../../../../../.." && pwd)/dist/common-lisp/hydra-kernel/src/main/common-lisp/hydra"
OUTFILE="$(dirname "$0")/struct-compat.lisp"

echo ";;; Auto-generated alist-compatible struct accessors" > "$OUTFILE"
echo ";;; Generated by gen-compat.sh — do not edit manually" >> "$OUTFILE"
echo "(in-package :cl-user)" >> "$OUTFILE"
echo "" >> "$OUTFILE"

# Function to convert CamelCase to snake_case
camel_to_snake() {
    echo "$1" | sed -E 's/([a-z0-9])([A-Z])/\1_\2/g' | tr '[:upper:]' '[:lower:]'
}

# Extract all cl:defstruct definitions
grep -h "cl:defstruct" "$GENDIR"/*.lisp "$GENDIR"/**/*.lisp 2>/dev/null | \
    sed 's/.*(cl:defstruct //' | sed 's/)//' | sort -u | \
while read -r line; do
    # Parse: StructName field1 field2 ...
    struct_name=$(echo "$line" | awk '{print $1}')
    fields=$(echo "$line" | awk '{$1=""; print $0}' | xargs)

    snake_name=$(camel_to_snake "$struct_name")

    echo "" >> "$OUTFILE"
    echo ";; $struct_name -> $snake_name" >> "$OUTFILE"

    # Constructor: (make-snake_name :field1 v1 :field2 v2)
    echo "(defun make-${snake_name} (&rest args)" >> "$OUTFILE"
    echo "  (if (and args (keywordp (first args)))" >> "$OUTFILE"
    echo "    (loop for (k v) on args by #'cddr collect (cons k v))" >> "$OUTFILE"

    # Build positional keyword list
    kw_list=""
    for f in $fields; do
        kw_list="$kw_list :${f}"
    done
    echo "    (mapcar #'cons '($kw_list) args)))" >> "$OUTFILE"

    # Accessors: (snake_name-field rec) -> (cdr (assoc :field rec))
    for f in $fields; do
        echo "(defun ${snake_name}-${f} (rec) (cdr (assoc :${f} rec)))" >> "$OUTFILE"
    done

    # Also create copy and predicate
    echo "(defun copy-${snake_name} (x) (copy-alist x))" >> "$OUTFILE"
    echo "(defun ${snake_name}-p (x) (listp x))" >> "$OUTFILE"
done

echo "" >> "$OUTFILE"
echo ";;; End of generated compatibility definitions" >> "$OUTFILE"

echo "Generated $(wc -l < "$OUTFILE") lines in $OUTFILE"
