pub fn graph(elements: Vec<crate::hydra::core::Binding>) -> String {
  let element_strs = crate::hydra::lib::lists::map(crate::hydra::show::core::binding, elements.clone()) ;
  crate::hydra::lib::strings::cat(Vec::from([
    String::from("{"),
    crate::hydra::lib::strings::intercalate(String::from(", "), element_strs.clone()),
    String::from("}")]))}
