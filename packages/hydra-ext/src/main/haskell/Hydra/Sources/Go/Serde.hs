unaryOpToExpr :: Go.UnaryOp -> Ast.Expr
unaryOpToExpr op = cst $ case op of
  Go.UnaryOpPlus -> "+"
  Go.UnaryOpMinus -> "-"
  Go.UnaryOpNot -> "!"
  Go.UnaryOpXor -> "^"
  Go.UnaryOpDeref -> "*"
  Go.UnaryOpAddressOf -> "&"
  Go.UnaryOpReceive -> "<-"

mulOpToExpr :: Go.MulOp -> Ast.Expr
mulOpToExpr op = cst $ case op of
  Go.MulOpMultiply -> "*"
  Go.MulOpDivide -> "/"
  Go.MulOpRemainder -> "%"
  Go.MulOpLeftShift -> "<<"
  Go.MulOpRightShift -> ">>"
  Go.MulOpBitwiseAnd -> "&"
  Go.MulOpBitClear -> "&^"

addOpToExpr :: Go.AddOp -> Ast.Expr
addOpToExpr op = cst $ case op of
  Go.AddOpAdd -> "+"
  Go.AddOpSubtract -> "-"
  Go.AddOpBitwiseOr -> "|"
  Go.AddOpBitwiseXor -> "^"

relOpToExpr :: Go.RelOp -> Ast.Expr
relOpToExpr op = cst $ case op of
  Go.RelOpEqual -> "=="
  Go.RelOpNotEqual -> "!="
  Go.RelOpLess -> "<"
  Go.RelOpLessEqual -> "<="
  Go.RelOpGreater -> ">"
  Go.RelOpGreaterEqual -> ">="

-- ============================================================================
-- Terminal Tokens
-- ============================================================================

identifierToExpr :: Go.Identifier -> Ast.Expr
identifierToExpr (Go.Identifier s) = cst s

intLitToExpr :: Go.IntLit -> Ast.Expr
intLitToExpr (Go.IntLit n) = cst $ show n

floatLitToExpr :: Go.FloatLit -> Ast.Expr
floatLitToExpr (Go.FloatLit d) = cst $ show d

imaginaryLitToExpr :: Go.ImaginaryLit -> Ast.Expr
imaginaryLitToExpr (Go.ImaginaryLit d) = cst $ show d ++ "i"

runeLitToExpr :: Go.RuneLit -> Ast.Expr
runeLitToExpr (Go.RuneLit c) = cst $ "'" ++ escapeRune (C.chr $ fromIntegral c) ++ "'"
  where
    escapeRune ch = case ch of
      '\'' -> "\\'"
      '\\' -> "\\\\"
      '\n' -> "\\n"
      '\r' -> "\\r"
      '\t' -> "\\t"
      _ -> if ord ch >= 32 && ord ch < 127
           then [ch]
           else if ord ch > 0xFFFF
             then -- Supplementary plane: use UTF-16 surrogate pair
               let n = ord ch - 0x10000
                   hi = 0xD800 + (n `div` 0x400)
                   lo = 0xDC00 + (n `mod` 0x400)
                   padH x = let h = fmap C.toUpper $ showHex x "" in
                            replicate (4 - L.length h) '0' ++ h
               in "\\u" ++ padH hi ++ "\\u" ++ padH lo
             else "\\u" ++ L.take (4 - L.length hex) "0000" ++ hex
               where hex = showHex (ord ch) ""

stringLitToExpr :: Go.StringLit -> Ast.Expr
stringLitToExpr lit = case lit of
  Go.StringLitRaw r -> rawStringLitToExpr r
  Go.StringLitInterpreted i -> interpretedStringLitToExpr i

rawStringLitToExpr :: Go.RawStringLit -> Ast.Expr
rawStringLitToExpr (Go.RawStringLit s) = cst $ "`" ++ s ++ "`"

interpretedStringLitToExpr :: Go.InterpretedStringLit -> Ast.Expr
interpretedStringLitToExpr (Go.InterpretedStringLit s) = cst $ "\"" ++ escapeGoString s ++ "\""
  where
    escapeGoString = concatMap escapeChar
    escapeChar c = case c of
      '"'  -> "\\\""
      '\\' -> "\\\\"
      '\n' -> "\\n"
      '\r' -> "\\r"
      '\t' -> "\\t"
      _ | c < ' ' || c > '~' -> goUnicodeEscape (ord c)
        | otherwise -> [c]
    goUnicodeEscape n
      | n > 0xFFFF = -- Supplementary plane: use UTF-16 surrogate pair
          let n' = n - 0x10000
              hi = 0xD800 + (n' `div` 0x400)
              lo = 0xDC00 + (n' `mod` 0x400)
          in "\\u" ++ padHex hi ++ "\\u" ++ padHex lo
      | otherwise = "\\u" ++ padHex n
    padHex n = replicate (4 - length hex) '0' ++ hex
      where hex = fmap toUpper $ showHex n ""

