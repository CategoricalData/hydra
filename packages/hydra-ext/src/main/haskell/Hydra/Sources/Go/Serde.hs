annotatedDeclarationToExpr :: Go.AnnotatedDeclaration -> Ast.Expr
annotatedDeclarationToExpr (Go.AnnotatedDeclaration comment decl) = newlineSep [
  commentToExpr comment,
  topLevelDeclToExpr decl]

commentToExpr :: String -> Ast.Expr
commentToExpr s = if L.null (lines s)
  then cst ""
  else newlineSep (singleLineComment <$> lines s)

-- ============================================================================
-- Helpers
-- ============================================================================

singleLineComment :: String -> Ast.Expr
singleLineComment c = cst $ "// " ++ sanitizeGoComment c

sanitizeGoComment :: String -> String
sanitizeGoComment = id  -- Go comments don't need special escaping like HTML
