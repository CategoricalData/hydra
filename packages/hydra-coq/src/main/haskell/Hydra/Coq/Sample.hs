-- | Sample Coq output demonstrating what the Coq coder generates.
-- This module constructs Coq AST manually and prints it, showing
-- the kind of .v files the coder will produce from Hydra modules.

module Hydra.Coq.Sample where

import Hydra.Coq.Syntax
import Hydra.Coq.Print

-- | Helper to construct a simple Ident
ident :: String -> Ident
ident s = Ident (String_ s)

-- | Helper to construct a simple Qualid
qualid :: String -> Qualid
qualid s = Qualid (ident s) []

-- | Helper to construct a Name
name :: String -> Name
name s = Name (Just (ident s))

-- | Construct a simple type reference (as a Term)
typeRef :: String -> Term
typeRef s = TermTerm100 $ Term100Term10 $ Term10OneTerm $ OneTermExplicit $
  QualidAnnotated (qualid s) Nothing

-- | Construct a Type from a type reference string
typ :: String -> Type
typ = Type . typeRef

-- | Construct an arrow type: a -> b (encoded as forall _ : a, b)
arrow :: Term -> Term -> Term
arrow a b = TermForallOrFun $ ForallOrFunForall $ Forall
  (OpenBindersBinders [BinderType $ TypeBinders [Name Nothing] (Type a)])
  (Type b)

-- | Construct a type application: F A
typeApp :: String -> [Term] -> Term
typeApp f [] = typeRef f
typeApp f args =
  TermTerm100 $ Term100Term10 $ Term10Application $ ApplicationNormal $
    NormalApplication
      (Term1Term0 $ Term0QualidAnnotated $ QualidAnnotated (qualid f) Nothing)
      (map (\a -> ArgTerm $ Term1Term0 $ Term0Parens a) args)

-- | Generate a sample Coq document that demonstrates Hydra type mappings
sampleDocument :: Document
sampleDocument = Document
  [ -- Require Import
    Sentence (Just $ Comment "Generated by Hydra Coq coder") $
      SentenceContentRequireImport $ RequireImport
        Nothing True (Just ImportQualificationImport)
        [qualid "Stdlib.Strings.String", qualid "Stdlib.Lists.List",
         qualid "Stdlib.ZArith.ZArith"]

  -- A simple enum (Hydra union with unit fields)
  , Sentence (Just $ Comment "Hydra union type -> Coq Inductive") $
      SentenceContentInductive $ InductiveDefinition Nothing False
        [InductiveBody (ident "Color") [] (Just $ typ "Type")
          [ Constructor (ident "Color_Red") [] Nothing
          , Constructor (ident "Color_Green") [] Nothing
          , Constructor (ident "Color_Blue") [] Nothing
          ]]

  -- A union with data (Hydra union with non-unit fields)
  , Sentence Nothing $
      SentenceContentInductive $ InductiveDefinition Nothing False
        [InductiveBody (ident "Shape") [] (Just $ typ "Type")
          [ Constructor (ident "Shape_Circle") []
              (Just $ Type $ arrow (typeRef "nat") (typeRef "Shape"))
          , Constructor (ident "Shape_Rectangle") []
              (Just $ Type $ arrow (typeRef "nat")
                (arrow (typeRef "nat") (typeRef "Shape")))
          ]]

  -- A record (Hydra record type -> Coq Record)
  , Sentence (Just $ Comment "Hydra record type -> Coq Record") $
      SentenceContentRecord $ RecordDefinition Nothing (ident "Person") []
        (Just SortType)
        (RecordBody (Just (ident "Build_Person"))
          [ RecordField (ident "person_name") (typ "string")
          , RecordField (ident "person_age") (typ "nat")
          ])

  -- A type alias (Hydra wrap type -> Coq Definition)
  , Sentence (Just $ Comment "Hydra wrap type -> Coq type alias") $
      SentenceContentDefinition $ Definition Nothing (ident "PersonId") []
        (Just $ typ "Type")
        (typeRef "string")

  -- A polymorphic type (Hydra forall -> Coq Inductive with parameter)
  , Sentence Nothing $
      SentenceContentInductive $ InductiveDefinition Nothing False
        [InductiveBody (ident "Maybe_")
          [BinderType $ TypeBinders [name "A"] (typ "Type")]
          (Just $ typ "Type")
          [ Constructor (ident "Maybe__Nothing") [] Nothing
          , Constructor (ident "Maybe__Just") []
              (Just $ Type $ arrow (typeRef "A") (typeApp "Maybe_" [typeRef "A"]))
          ]]

  -- A function definition
  , Sentence (Just $ Comment "Hydra term definition -> Coq Definition") $
      SentenceContentDefinition $ Definition Nothing (ident "is_red") []
        Nothing
        (TermForallOrFun $ ForallOrFunFun $ Fun
          (OpenBindersBinders [BinderName (name "c")])
          (TermTerm100 $ Term100Term10 $ Term10OneTerm $ OneTermTerm1 $
            Term1Term0 $ Term0Match $ Match
              [CaseItem (Term100Term10 $ Term10OneTerm $ OneTermExplicit $
                QualidAnnotated (qualid "c") Nothing) Nothing Nothing]
              (Just $ Term100Term10 $ Term10OneTerm $ OneTermExplicit $
                QualidAnnotated (qualid "bool") Nothing)
              True
              [ Equation [[PatternPattern $ Pattern10Qualiid $
                  Pattern10_Qualid (qualid "Color_Red") []]]
                  (typeRef "true")
              , Equation [[PatternPattern $ Pattern10Qualiid $
                  Pattern10_Qualid (qualid "_") []]]
                  (typeRef "false")
              ]))

  -- A theorem demonstrating what proofs we can generate
  , Sentence (Just $ Comment "Machine-checked property: unit is left identity for products") $
      SentenceContentTheorem $ TheoremBody TheoremKindLemma
        (ident "unit_left_identity")
        [BinderType $ TypeBinders [name "A"] (typ "Type")]
        (Type $ arrow
          (typeApp "prod" [typeRef "unit", typeRef "A"])
          (typeRef "A"))
        -- Proof term: fun p => snd p
        (TermForallOrFun $ ForallOrFunFun $ Fun
          (OpenBindersBinders [BinderName (name "p")])
          (TermTerm100 $ Term100Term10 $ Term10Application $ ApplicationNormal $
            NormalApplication
              (Term1Term0 $ Term0QualidAnnotated $ QualidAnnotated (qualid "snd") Nothing)
              [ArgTerm $ Term1Term0 $ Term0QualidAnnotated $
                QualidAnnotated (qualid "p") Nothing]))
  ]

-- | Print the sample document as a .v file
printSample :: String
printSample = printDocument sampleDocument
