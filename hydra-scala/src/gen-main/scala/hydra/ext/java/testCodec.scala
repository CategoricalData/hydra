def generateJavaTestGroupHierarchy(g: hydra.graph.Graph)(codec: hydra.testing.TestCodec)(groupPath: Seq[scala.Predef.String])(testGroup: hydra.testing.TestGroup): Either[scala.Predef.String,
   scala.Predef.String] =
  {
  val `cases_`: Seq[hydra.testing.TestCaseWithMetadata] = (testGroup.cases)
  val subgroups: Seq[hydra.testing.TestGroup] = (testGroup.subgroups)
  eithers.bind[scala.Predef.String, scala.Predef.String, scala.Predef.String](eithers.map[Seq[Seq[scala.Predef.String]],
     scala.Predef.String, scala.Predef.String]((`lines_`: Seq[Seq[scala.Predef.String]]) =>
    strings.intercalate("\n\n")(lists.concat[scala.Predef.String](`lines_`)))(eithers.mapList[hydra.testing.TestCaseWithMetadata,
       Seq[scala.Predef.String], scala.Predef.String]((tc: hydra.testing.TestCaseWithMetadata) =>
    hydra.ext.java.testCodec.generateJavaTestCase(g)(codec)(groupPath)(tc))(`cases_`)))((testCasesStr: scala.Predef.String) =>
    eithers.map[scala.Predef.String, scala.Predef.String, scala.Predef.String]((subgroupsStr: scala.Predef.String) =>
    strings.cat(Seq(testCasesStr, logic.ifElse[scala.Predef.String](logic.or(equality.equal[scala.Predef.String](testCasesStr)(""))(equality.equal[scala.Predef.String](subgroupsStr)("")))("")("\n\n"),
       subgroupsStr)))(eithers.map[Seq[scala.Predef.String], scala.Predef.String, scala.Predef.String]((blocks: Seq[scala.Predef.String]) => strings.intercalate("\n\n")(blocks))(eithers.mapList[hydra.testing.TestGroup,
       scala.Predef.String, scala.Predef.String]((subgroup: hydra.testing.TestGroup) =>
    {
    val groupName: scala.Predef.String = (subgroup.name)
    val header: scala.Predef.String = strings.cat2("    // ")(groupName)
    eithers.map[scala.Predef.String, scala.Predef.String, scala.Predef.String]((content: scala.Predef.String) => strings.cat(Seq(header,
       "\n\n", content)))(hydra.ext.java.testCodec.generateJavaTestGroupHierarchy(g)(codec)(lists.concat2[scala.Predef.String](groupPath)(Seq(groupName)))(subgroup))
  })(subgroups))))
}

def generateJavaTestCase(g: hydra.graph.Graph)(codec: hydra.testing.TestCodec)(groupPath: Seq[scala.Predef.String])(tcm: hydra.testing.TestCaseWithMetadata): Either[scala.Predef.String,
   Seq[scala.Predef.String]] =
  {
  val `name_`: scala.Predef.String = (tcm.name)
  val tcase: hydra.testing.TestCase = (tcm.`case`)
  tcase match
    case hydra.testing.TestCase.delegatedEvaluation(v_TestCase_delegatedEvaluation_delCase) => {
      val `input_`: hydra.core.Term = (v_TestCase_delegatedEvaluation_delCase.input)
      val `output_`: hydra.core.Term = (v_TestCase_delegatedEvaluation_delCase.output)
      val fullName: scala.Predef.String = logic.ifElse[scala.Predef.String](lists.`null`[scala.Predef.String](groupPath))(`name_`)(strings.intercalate("_")(lists.concat2[scala.Predef.String](groupPath)(Seq(`name_`))))
      val formattedName: scala.Predef.String = codec.formatTestName(fullName)
      val assertType: scala.Predef.String = hydra.ext.java.testCodec.getAssertionType(`output_`)
      {
        val typeVars: Seq[hydra.core.Name] = lists.sort[hydra.core.Name](lists.filter[hydra.core.Name](hydra.ext.java.testCodec.isInferenceVar)(sets.toList[hydra.core.Name](sets.union[hydra.core.Name](hydra.rewriting.freeTypeVariablesInTerm(`input_`))(hydra.rewriting.freeTypeVariablesInTerm(`output_`)))))
        val typeParamsStr: scala.Predef.String = logic.ifElse[scala.Predef.String](lists.`null`[hydra.core.Name](typeVars))("")(strings.cat(Seq("<",
           strings.intercalate(", ")(lists.map[hydra.core.Name, scala.Predef.String]((`n_`: hydra.core.Name) => hydra.formatting.capitalize(`n_`))(typeVars)),
           "> ")))
        eithers.bind[scala.Predef.String, scala.Predef.String, Seq[scala.Predef.String]](codec.encodeTerm(`input_`)(g))((inputCode: scala.Predef.String) =>
          eithers.map[scala.Predef.String, Seq[scala.Predef.String], scala.Predef.String]((outputCode: scala.Predef.String) =>
          {
          val assertionLines: Seq[scala.Predef.String] = hydra.ext.java.testCodec.generateAssertion(assertType)(outputCode)(inputCode)
          lists.concat2[scala.Predef.String](Seq("    @Test", strings.cat(Seq("    public ", typeParamsStr,
             "void ", formattedName, "() {"))))(lists.concat2[scala.Predef.String](assertionLines)(Seq("    }")))
        })(codec.encodeTerm(`output_`)(g)))
      }
    }
    case _ => Right(Seq())
}

def getAssertionType(term: hydra.core.Term): scala.Predef.String =
  hydra.rewriting.deannotateTerm(term) match
  case hydra.core.Term.literal(v_Term_literal_lit) => v_Term_literal_lit match
    case hydra.core.Literal.binary(v_Literal_binary__b) => "assertArrayEquals"
    case hydra.core.Literal.float(v_Literal_float_fv) => v_Literal_float_fv match
      case hydra.core.FloatValue.bigfloat(v_FloatValue_bigfloat__bf) => "assertBigDecimalEquals"
      case _ => "assertDoubleEquals"
    case _ => "assertEquals"
  case _ => "assertEquals"

def generateAssertion(assertType: scala.Predef.String)(outputCode: scala.Predef.String)(inputCode: scala.Predef.String): Seq[scala.Predef.String] =
  logic.ifElse[Seq[scala.Predef.String]](equality.equal[scala.Predef.String](assertType)("assertArrayEquals"))(Seq("        assertArrayEquals(",
     strings.cat(Seq("            ", outputCode, ",")), strings.cat(Seq("            ", inputCode, ");"))))(logic.ifElse[Seq[scala.Predef.String]](equality.equal[scala.Predef.String](assertType)("assertBigDecimalEquals"))(Seq(strings.cat(Seq("        assertEquals(0, (",
     outputCode, ").compareTo(", inputCode, "));"))))(logic.ifElse[Seq[scala.Predef.String]](equality.equal[scala.Predef.String](assertType)("assertDoubleEquals"))(Seq("        assertEquals(",
     strings.cat(Seq("            ", outputCode, ",")), strings.cat(Seq("            ", inputCode, ",")),
     "            1e-15);"))(Seq("        assertEquals(", strings.cat(Seq("            ", outputCode,
     ",")), strings.cat(Seq("            ", inputCode, ");"))))))

def isInferenceVar(n: hydra.core.Name): Boolean =
  {
  val s: scala.Predef.String = n
  val chars: Seq[Int] = strings.toList(s)
  logic.and(equality.equal[Int](strings.charAt(0)(s))(116))(logic.and(logic.not(equality.equal[Int](strings.length(s))(1)))(lists.`null`[Int](lists.filter[Int]((c: Int) =>
    logic.not(logic.and(equality.gte[Int](c)(48))(equality.lte[Int](c)(57))))(lists.drop[Int](1)(chars)))))
}

def generateTestFileWithJavaCodec(codec: hydra.testing.TestCodec)(testModule: hydra.module.Module)(testGroup: hydra.testing.TestGroup)(g: hydra.graph.Graph): Either[scala.Predef.String,
   Tuple2[scala.Predef.String, scala.Predef.String]] =
  eithers.map[scala.Predef.String, Tuple2[scala.Predef.String, scala.Predef.String], scala.Predef.String]((testBody: scala.Predef.String) =>
  {
  val testModuleContent: scala.Predef.String = hydra.ext.java.testCodec.buildJavaTestModule(codec)(testModule)(testGroup)(testBody)
  val `ns_`: hydra.module.Namespace = (testModule.namespace)
  val parts: Seq[scala.Predef.String] = strings.splitOn(".")(`ns_`)
  val dirParts: Seq[scala.Predef.String] = lists.drop[scala.Predef.String](1)(lists.init[scala.Predef.String](parts))
  val `className_`: scala.Predef.String = strings.cat2(hydra.formatting.capitalize(lists.last[scala.Predef.String](parts)))("Test")
  val fileName: scala.Predef.String = strings.cat2(`className_`)(".java")
  val filePath: scala.Predef.String = strings.cat(Seq(strings.intercalate("/")(dirParts), "/", fileName))
  Tuple2(filePath, testModuleContent)
})(hydra.ext.java.testCodec.generateJavaTestGroupHierarchy(g)(codec)(Seq())(testGroup))

def buildJavaTestModule[T0](codec: T0)(testModule: hydra.module.Module)(testGroup: hydra.testing.TestGroup)(testBody: scala.Predef.String): scala.Predef.String =
  {
  val `ns_`: hydra.module.Namespace = (testModule.namespace)
  val parts: Seq[scala.Predef.String] = strings.splitOn(".")(`ns_`)
  val packageName: scala.Predef.String = strings.intercalate(".")(lists.init[scala.Predef.String](parts))
  val `className_`: scala.Predef.String = strings.cat2(hydra.formatting.capitalize(lists.last[scala.Predef.String](parts)))("Test")
  val `groupName_`: scala.Predef.String = (testGroup.name)
  val standardImports: Seq[scala.Predef.String] = Seq("import org.junit.jupiter.api.Test;", "import static org.junit.jupiter.api.Assertions.*;",
     "import java.util.*;", "import hydra.util.*;")
  val header: scala.Predef.String = strings.cat(Seq(strings.cat2("// ")(hydra.constants.warningAutoGeneratedFile),
     "\n", strings.cat2("// ")(`groupName_`), "\n\n", strings.cat(Seq("package ", packageName, ";\n\n")),
     strings.intercalate("\n")(standardImports), "\n\n", strings.cat(Seq("public class ", `className_`,
     " {\n\n"))))
  strings.cat(Seq(header, testBody, "\n}\n"))
}

def generateJavaTestFile(testModule: hydra.module.Module)(testGroup: hydra.testing.TestGroup)(g: hydra.graph.Graph): Either[scala.Predef.String,
   Tuple2[scala.Predef.String, scala.Predef.String]] =
  eithers.bind[scala.Predef.String, hydra.testing.TestGroup, Tuple2[scala.Predef.String, scala.Predef.String]](hydra.test.utils.inferTestGroupTerms(g)(testGroup))((inferredTestGroup: hydra.testing.TestGroup) =>
  hydra.ext.java.testCodec.generateTestFileWithJavaCodec(hydra.ext.java.testCodec.javaTestCodec)(testModule)(inferredTestGroup)(g))
