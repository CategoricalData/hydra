def formatJavaTestName(name: scala.Predef.String): scala.Predef.String =
  {
  val replaced: scala.Predef.String = strings.intercalate(" Neg")(strings.splitOn("-")(strings.intercalate("Dot")(strings.splitOn(".")(strings.intercalate(" Plus")(strings.splitOn("+")(strings.intercalate(" Div")(strings.splitOn("/")(strings.intercalate(" Mul")(strings.splitOn("*")(strings.intercalate(" Num")(strings.splitOn("#")(name))))))))))))
  val sanitized: scala.Predef.String = hydra.formatting.nonAlnumToUnderscores(replaced)
  val `pascal_`: scala.Predef.String = hydra.formatting.convertCase(hydra.util.CaseConvention.lowerSnake)(hydra.util.CaseConvention.pascal)(sanitized)
  strings.cat2("test")(`pascal_`)
}

