def termToJava(term: hydra.core.Term)(g: hydra.graph.Graph): Either[scala.Predef.String, scala.Predef.String] =
  eithers.bimap[hydra.context.InContext[hydra.error.Error], hydra.ext.java.syntax.Expression, scala.Predef.String,
     scala.Predef.String]((ic: hydra.context.InContext[hydra.error.Error]) => hydra.show.error.error(ic.`object`))((`arg_`: hydra.ext.java.syntax.Expression) =>
  hydra.serialization.printExpr(hydra.serialization.parenthesize(hydra.ext.java.serde.writeExpression(`arg_`))))(hydra.ext.java.coder.encodeTerm(hydra.ext.java.helpers.JavaEnvironment(hydra.ext.java.helpers.Aliases("test",
     maps.empty[hydra.module.Namespace, hydra.ext.java.syntax.PackageName], sets.empty[hydra.core.Name],
     sets.empty[hydra.core.Name], sets.empty[hydra.core.Name], sets.empty[hydra.core.Name], sets.empty[hydra.core.Name],
     maps.empty[hydra.core.Name, hydra.core.Name], sets.empty[hydra.core.Name], maps.empty[hydra.core.Name,
     hydra.core.Name], sets.empty[hydra.core.Name], None, sets.empty[hydra.core.Name]), g))(term)(hydra.lexical.emptyContext)(g))

