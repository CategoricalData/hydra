})(hydra.ext.haskell.testCodec.generateTestGroupHierarchy(g)(namespaces)(codec)(1)(testGroup))

def extractEncodedTermVariableNames(graf: hydra.graph.Graph)(term: hydra.core.Term): scala.collection.immutable.Set[hydra.core.Name] =
  hydra.rewriting.foldOverTerm(hydra.coders.TraversalOrder.pre)((v1: scala.collection.immutable.Set[hydra.core.Name]) =>
  (v2: hydra.core.Term) => hydra.ext.haskell.testCodec.collectNames(graf)(v1)(v2))(sets.empty[hydra.core.Name])(term)

def collectNames(graf: hydra.graph.Graph)(names: scala.collection.immutable.Set[hydra.core.Name])(t: hydra.core.Term): scala.collection.immutable.Set[hydra.core.Name] =
  logic.ifElse[scala.collection.immutable.Set[hydra.core.Name]](hydra.schemas.isEncodedTerm(hydra.rewriting.deannotateTerm(t)))(eithers.either[hydra.error.DecodingError,
     hydra.core.Term, scala.collection.immutable.Set[hydra.core.Name]]((_x: hydra.error.DecodingError) => names)((decodedTerm: hydra.core.Term) =>
  sets.union[hydra.core.Name](names)(hydra.rewriting.termDependencyNames(true)(true)(true)(decodedTerm)))(eithers.bimap[hydra.error.DecodingError,
     hydra.core.Term, hydra.error.DecodingError, hydra.core.Term]((_e: hydra.error.DecodingError) => _e)((_a: hydra.core.Term) => _a)(hydra.decode.core.term(graf)(t))))(names)

def addNamespacesToNamespaces(ns0: hydra.module.Namespaces[hydra.ext.haskell.ast.ModuleName])(names: scala.collection.immutable.Set[hydra.core.Name]): hydra.module.Namespaces[hydra.ext.haskell.ast.ModuleName] =
  {
  val newNamespaces: scala.collection.immutable.Set[hydra.module.Namespace] = sets.fromList[hydra.module.Namespace](maybes.cat[hydra.module.Namespace](lists.map[hydra.core.Name,
     Option[hydra.module.Namespace]](hydra.names.namespaceOf)(sets.toList[hydra.core.Name](names))))
  def toModuleName(namespace: hydra.module.Namespace): hydra.ext.haskell.ast.ModuleName =
    hydra.formatting.capitalize(lists.last[scala.Predef.String](strings.splitOn(".")(namespace)))
  val newMappings: Map[hydra.module.Namespace, hydra.ext.haskell.ast.ModuleName] = maps.fromList[hydra.module.Namespace,
     hydra.ext.haskell.ast.ModuleName](lists.map[hydra.module.Namespace, Tuple2[hydra.module.Namespace,
     hydra.ext.haskell.ast.ModuleName]]((`ns_`: hydra.module.Namespace) => Tuple2(`ns_`, toModuleName(`ns_`)))(sets.toList[hydra.module.Namespace](newNamespaces)))
  hydra.module.Namespaces(ns0.focus, maps.union[hydra.module.Namespace, hydra.ext.haskell.ast.ModuleName](ns0.mapping)(newMappings))
}

