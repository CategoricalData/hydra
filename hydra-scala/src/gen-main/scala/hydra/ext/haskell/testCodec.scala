def buildNamespacesForTestGroup(mod: hydra.module.Module)(tgroup: hydra.testing.TestGroup)(`graph_`: hydra.graph.Graph): Either[scala.Predef.String,
   hydra.module.Namespaces[hydra.ext.haskell.ast.ModuleName]] =
  {
  val `testCases_`: Seq[hydra.testing.TestCaseWithMetadata] = hydra.ext.haskell.testCodec.collectTestCases(tgroup)
  val testTerms: Seq[hydra.core.Term] = lists.concat[hydra.core.Term](lists.map[hydra.testing.TestCaseWithMetadata,
     Seq[hydra.core.Term]](hydra.ext.haskell.testCodec.extractTestTerms)(`testCases_`))
  val testBindings: Seq[hydra.core.Binding] = lists.map[hydra.core.Term, hydra.core.Binding]((term: hydra.core.Term) => hydra.core.Binding("_test_",
     term, None))(testTerms)
  val tempModule: hydra.module.Module = hydra.module.Module(mod.namespace, testBindings, (mod.termDependencies), (mod.typeDependencies), (mod.description))
  eithers.bind[scala.Predef.String, hydra.module.Namespaces[hydra.ext.haskell.ast.ModuleName], hydra.module.Namespaces[hydra.ext.haskell.ast.ModuleName]](eithers.bimap[hydra.context.InContext[hydra.error.Error],
     hydra.module.Namespaces[hydra.ext.haskell.ast.ModuleName], scala.Predef.String, hydra.module.Namespaces[hydra.ext.haskell.ast.ModuleName]]((ic: hydra.context.InContext[hydra.error.Error]) => hydra.show.error.error(ic.`object`))((a: hydra.module.Namespaces[hydra.ext.haskell.ast.ModuleName]) => a)(hydra.ext.haskell.utils.namespacesForModule(tempModule)(hydra.lexical.emptyContext)(`graph_`)))((baseNamespaces: hydra.module.Namespaces[hydra.ext.haskell.ast.ModuleName]) =>
    {
    val encodedNames: scala.collection.immutable.Set[hydra.core.Name] = sets.unions[hydra.core.Name](lists.map[hydra.core.Term,
       scala.collection.immutable.Set[hydra.core.Name]]((t: hydra.core.Term) =>
      hydra.ext.haskell.testCodec.extractEncodedTermVariableNames(`graph_`)(t))(testTerms))
    Right(hydra.ext.haskell.testCodec.addNamespacesToNamespaces(baseNamespaces)(encodedNames))
  })
}

def collectTestCases(tg: hydra.testing.TestGroup): Seq[hydra.testing.TestCaseWithMetadata] =
  lists.concat2[hydra.testing.TestCaseWithMetadata](tg.cases)(lists.concat[hydra.testing.TestCaseWithMetadata](lists.map[hydra.testing.TestGroup,
     Seq[hydra.testing.TestCaseWithMetadata]](hydra.ext.haskell.testCodec.collectTestCases)(tg.subgroups)))

def extractTestTerms(tcm: hydra.testing.TestCaseWithMetadata): Seq[hydra.core.Term] =
  tcm.`case` match
  case hydra.testing.TestCase.delegatedEvaluation(v_TestCase_delegatedEvaluation_delCase) => Seq(v_TestCase_delegatedEvaluation_delCase.input,
     (v_TestCase_delegatedEvaluation_delCase.output))
  case _ => Seq()
