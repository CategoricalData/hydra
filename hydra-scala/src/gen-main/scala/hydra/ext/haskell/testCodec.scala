def termToHaskell(namespaces: hydra.module.Namespaces[hydra.ext.haskell.ast.ModuleName])(term: hydra.core.Term)(g: hydra.graph.Graph): Either[scala.Predef.String,
   scala.Predef.String] =
  eithers.bimap[hydra.context.InContext[hydra.error.Error], hydra.ext.haskell.ast.Expression, scala.Predef.String,
     scala.Predef.String]((ic: hydra.context.InContext[hydra.error.Error]) => hydra.show.error.error(ic.`object`))((`arg_`: hydra.ext.haskell.ast.Expression) =>
  hydra.serialization.printExpr(hydra.serialization.parenthesize(hydra.ext.haskell.serde.expressionToExpr(`arg_`))))(hydra.ext.haskell.coder.encodeTerm(0)(namespaces)(term)(hydra.lexical.emptyContext)(g))

def typeToHaskell[T0](namespaces: hydra.module.Namespaces[hydra.ext.haskell.ast.ModuleName])(typ: hydra.core.Type)(g: T0): Either[scala.Predef.String,
   scala.Predef.String] =
  eithers.bimap[hydra.context.InContext[hydra.error.Error], hydra.ext.haskell.ast.Type, scala.Predef.String,
     scala.Predef.String]((ic: hydra.context.InContext[hydra.error.Error]) => hydra.show.error.error(ic.`object`))((`arg_`: hydra.ext.haskell.ast.Type) =>
  hydra.serialization.printExpr(hydra.serialization.parenthesize(hydra.ext.haskell.serde.typeToExpr(`arg_`))))(hydra.ext.haskell.coder.encodeType(namespaces)(typ)(hydra.lexical.emptyContext)(g))

def haskellTestCodec(namespaces: hydra.module.Namespaces[hydra.ext.haskell.ast.ModuleName]): hydra.testing.TestCodec =
  hydra.testing.TestCodec("haskell", "hs", (v1: hydra.core.Term) =>
  (v2: hydra.graph.Graph) =>
  hydra.ext.haskell.testCodec.termToHaskell(namespaces)(v1)(v2), (v1: hydra.core.Type) =>
  (v2: hydra.graph.Graph) =>
  hydra.ext.haskell.testCodec.typeToHaskell(namespaces)(v1)(v2), (n: scala.Predef.String) => n, hydra.ext.haskell.testCodec.namespaceToModuleName,
     hydra.ext.haskell.testCodec.haskellTestCaseTemplate, hydra.ext.haskell.testCodec.haskellTestGroupTemplate,
     hydra.ext.haskell.testCodec.haskellModuleTemplate, hydra.ext.haskell.testCodec.haskellImportTemplate,
     (v1: scala.collection.immutable.Set[hydra.core.Name]) =>
  hydra.ext.haskell.testCodec.findHaskellImports(namespaces)(v1))

val haskellTestCaseTemplate: scala.Predef.String = strings.intercalate("\n")(Seq("  H.it {name} $ H.shouldBe", "    ({input})", "    ({output})", ""))

val haskellTestGroupTemplate: scala.Predef.String = "spec = H.describe {groupName} $ do"

val haskellModuleTemplate: scala.Predef.String = strings.intercalate("\n")(Seq(strings.cat2("-- ")(hydra.constants.warningAutoGeneratedFile),
   "", "module {moduleName} where", "", "{imports}", "", "spec :: H.Spec", "{testGroup}", "{testCases}",
   ""))

val haskellImportTemplate: scala.Predef.String = "import qualified {namespace} as {alias}"

def findHaskellImports[T0](namespaces: hydra.module.Namespaces[hydra.ext.haskell.ast.ModuleName])(`names_`: T0): Seq[scala.Predef.String] =
  {
  val `mapping_`: Map[hydra.module.Namespace, hydra.ext.haskell.ast.ModuleName] = (namespaces.mapping)
  val filtered: Map[hydra.module.Namespace, hydra.ext.haskell.ast.ModuleName] = maps.filterWithKey[hydra.module.Namespace,
     hydra.ext.haskell.ast.ModuleName]((`ns_`: hydra.module.Namespace) =>
    (_v: hydra.ext.haskell.ast.ModuleName) =>
    logic.not(equality.equal[scala.Predef.String](lists.head[scala.Predef.String](strings.splitOn("hydra.test.")(`ns_`)))("")))(`mapping_`)
  lists.map[Tuple2[hydra.module.Namespace, hydra.ext.haskell.ast.ModuleName], scala.Predef.String]((entry: Tuple2[hydra.module.Namespace,
     hydra.ext.haskell.ast.ModuleName]) =>
    strings.cat(Seq("import qualified ", strings.intercalate(".")(lists.map[scala.Predef.String, scala.Predef.String](hydra.formatting.capitalize)(strings.splitOn(".")(pairs.first[hydra.module.Namespace,
       hydra.ext.haskell.ast.ModuleName](entry)))), " as ", pairs.second[hydra.module.Namespace, hydra.ext.haskell.ast.ModuleName](entry))))(maps.toList[hydra.module.Namespace,
       hydra.ext.haskell.ast.ModuleName](filtered))
}

def namespaceToModuleName(`ns_`: hydra.module.Namespace): scala.Predef.String =
  strings.intercalate(".")(lists.map[scala.Predef.String, scala.Predef.String](hydra.formatting.capitalize)(strings.splitOn(".")(`ns_`)))

def generateTestGroupHierarchy(g: hydra.graph.Graph)(namespaces: hydra.module.Namespaces[hydra.ext.haskell.ast.ModuleName])(codec: hydra.testing.TestCodec)(depth: Int)(testGroup: hydra.testing.TestGroup): Either[scala.Predef.String,
   scala.Predef.String] =
  {
  val `cases_`: Seq[hydra.testing.TestCaseWithMetadata] = (testGroup.cases)
  val subgroups: Seq[hydra.testing.TestGroup] = (testGroup.subgroups)
  val indent: scala.Predef.String = strings.fromList(lists.replicate[Int](math.mul(depth)(2))(32))
  eithers.bind[scala.Predef.String, Seq[Seq[scala.Predef.String]], scala.Predef.String](eithers.mapList[hydra.testing.TestCaseWithMetadata,
     Seq[scala.Predef.String], scala.Predef.String]((tc: hydra.testing.TestCaseWithMetadata) =>
    hydra.ext.haskell.testCodec.generateTestCaseWithCodec(g)(namespaces)(codec)(depth)(tc))(`cases_`))((testCaseLinesRaw: Seq[Seq[scala.Predef.String]]) =>
    {
    val testCaseLines: Seq[Seq[scala.Predef.String]] = lists.map[Seq[scala.Predef.String], Seq[scala.Predef.String]]((`lines_`: Seq[scala.Predef.String]) =>
      lists.map[scala.Predef.String, scala.Predef.String]((line: scala.Predef.String) => strings.cat2(indent)(line))(`lines_`))(testCaseLinesRaw)
    val testCasesStr: scala.Predef.String = strings.intercalate("\n")(lists.concat[scala.Predef.String](testCaseLines))
    eithers.map[scala.Predef.String, scala.Predef.String, scala.Predef.String]((subgroupsStr: scala.Predef.String) =>
      strings.cat(Seq(testCasesStr, logic.ifElse[scala.Predef.String](logic.or(equality.equal[scala.Predef.String](testCasesStr)(""))(equality.equal[scala.Predef.String](subgroupsStr)("")))("")("\n"),
         subgroupsStr)))(eithers.map[Seq[scala.Predef.String], scala.Predef.String, scala.Predef.String]((blocks: Seq[scala.Predef.String]) => strings.intercalate("\n")(blocks))(eithers.mapList[hydra.testing.TestGroup,
         scala.Predef.String, scala.Predef.String]((subgroup: hydra.testing.TestGroup) =>
      {
      val `groupName_`: scala.Predef.String = (subgroup.name)
      eithers.map[scala.Predef.String, scala.Predef.String, scala.Predef.String]((content: scala.Predef.String) =>
        strings.cat(Seq(indent, "H.describe ", literals.showString(`groupName_`), " $ do\n", content)))(hydra.ext.haskell.testCodec.generateTestGroupHierarchy(g)(namespaces)(codec)(math.add(depth)(1))(subgroup))
    })(subgroups)))
  })
}

def generateTestCaseWithCodec(g: hydra.graph.Graph)(namespaces: hydra.module.Namespaces[hydra.ext.haskell.ast.ModuleName])(codec: hydra.testing.TestCodec)(depth: Int)(tcm: hydra.testing.TestCaseWithMetadata): Either[scala.Predef.String,
   Seq[scala.Predef.String]] =
  {
  val `name_`: scala.Predef.String = (tcm.name)
  val tcase: hydra.testing.TestCase = (tcm.`case`)
  tcase match
    case hydra.testing.TestCase.delegatedEvaluation(v_TestCase_delegatedEvaluation_delCase) => {
      val `input_`: hydra.core.Term = (v_TestCase_delegatedEvaluation_delCase.input)
      val `output_`: hydra.core.Term = (v_TestCase_delegatedEvaluation_delCase.output)
      val formattedName: scala.Predef.String = codec.formatTestName(`name_`)
      val continuationIndent: Int = math.add(math.mul(depth)(2))(4)
      eithers.bind[scala.Predef.String, scala.Predef.String, Seq[scala.Predef.String]](codec.encodeTerm(`input_`)(g))((inputCode: scala.Predef.String) =>
        eithers.bind[scala.Predef.String, scala.Predef.String, Seq[scala.Predef.String]](codec.encodeTerm(`output_`)(g))((outputCode: scala.Predef.String) =>
        eithers.bind[scala.Predef.String, Option[scala.Predef.String], Seq[scala.Predef.String]](hydra.ext.haskell.testCodec.generateTypeAnnotationFor(g)(namespaces)(`input_`)(`output_`))((typeAnnotation: Option[scala.Predef.String]) =>
        {
        val indentedInputCode: scala.Predef.String = hydra.ext.haskell.testCodec.indentContinuationLines(continuationIndent)(inputCode)
        val indentedOutputCode: scala.Predef.String = hydra.ext.haskell.testCodec.indentContinuationLines(continuationIndent)(outputCode)
        val finalOutputCode: scala.Predef.String = maybes.maybe[scala.Predef.String, scala.Predef.String](indentedOutputCode)((anno: scala.Predef.String) => strings.cat2(indentedOutputCode)(anno))(typeAnnotation)
        Right(Seq(strings.cat(Seq("H.it ", literals.showString(formattedName), " $ H.shouldBe")), strings.cat(Seq("  (",
           indentedInputCode, ")")), strings.cat(Seq("  (", finalOutputCode, ")"))))
      })))
    }
    case _ => Right(Seq())
}

def indentContinuationLines(n: Int)(s: scala.Predef.String): scala.Predef.String =
  strings.intercalate(strings.cat2("\n")(strings.fromList(lists.replicate[Int](n)(32))))(strings.splitOn("\n")(s))

def generateTypeAnnotationFor(g: hydra.graph.Graph)(namespaces: hydra.module.Namespaces[hydra.ext.haskell.ast.ModuleName])(inputTerm: hydra.core.Term)(outputTerm: hydra.core.Term): Either[scala.Predef.String,
   Option[scala.Predef.String]] =
  logic.ifElse[Either[scala.Predef.String, Option[scala.Predef.String]]](logic.not(hydra.ext.haskell.testCodec.containsTriviallyPolymorphic(outputTerm)))(Right(None))(maybes.maybe[Either[scala.Predef.String,
     Option[scala.Predef.String]], Tuple2[hydra.core.Term, hydra.core.TypeScheme]](Right(None))((result: Tuple2[hydra.core.Term,
     hydra.core.TypeScheme]) =>
  {
  val typeScheme: hydra.core.TypeScheme = pairs.second[hydra.core.Term, hydra.core.TypeScheme](result)
  val typ: hydra.core.Type = (typeScheme.`type`)
  val schemaVars: scala.collection.immutable.Set[hydra.core.Name] = sets.fromList[hydra.core.Name](maps.keys[hydra.core.Name,
     hydra.core.TypeScheme](g.schemaTypes))
  val freeVars: Seq[hydra.core.Name] = sets.toList[hydra.core.Name](sets.difference[hydra.core.Name](hydra.rewriting.freeVariablesInType(typ))(schemaVars))
  val isEither: Boolean = hydra.rewriting.deannotateTerm(outputTerm) match
    case hydra.core.Term.either(v_Term_either__) => true
    case _ => false
  logic.ifElse[Either[scala.Predef.String, Option[scala.Predef.String]]](logic.or(isEither)(logic.not(lists.`null`[hydra.core.Name](freeVars))))({
    val int32Type: hydra.core.Type = hydra.core.Type.literal(hydra.core.LiteralType.integer(hydra.core.IntegerType.int32))
    val subst: hydra.typing.TypeSubst = maps.fromList[hydra.core.Name, hydra.core.Type](lists.map[hydra.core.Name,
       Tuple2[hydra.core.Name, hydra.core.Type]]((v: hydra.core.Name) => Tuple2(v, int32Type))(freeVars))
    val groundedType: hydra.core.Type = hydra.substitution.substInType(subst)(typ)
    eithers.map[scala.Predef.String, Option[scala.Predef.String], scala.Predef.String]((typeStr: scala.Predef.String) => Some(strings.cat2(" :: ")(typeStr)))(hydra.ext.haskell.testCodec.typeToHaskell(namespaces)(groundedType)(g))
  })(Right(None))
})(hydra.ext.haskell.testCodec.tryInferTypeOf(g)(inputTerm)))

def tryInferTypeOf(g: hydra.graph.Graph)(term: hydra.core.Term): Option[Tuple2[hydra.core.Term, hydra.core.TypeScheme]] =
  eithers.either[hydra.context.InContext[hydra.error.Error], Tuple2[Tuple2[hydra.core.Term, hydra.core.TypeScheme],
     hydra.context.Context], Option[Tuple2[hydra.core.Term, hydra.core.TypeScheme]]]((_x: hydra.context.InContext[hydra.error.Error]) => None)((result: Tuple2[Tuple2[hydra.core.Term,
     hydra.core.TypeScheme], hydra.context.Context]) =>
  Some(pairs.first[Tuple2[hydra.core.Term, hydra.core.TypeScheme], hydra.context.Context](result)))(hydra.inference.inferTypeOf(hydra.lexical.emptyContext)(g)(term))

def containsTriviallyPolymorphic(term: hydra.core.Term): Boolean =
  term match
  case hydra.core.Term.list(v_Term_list_xs) => logic.or(lists.`null`[hydra.core.Term](v_Term_list_xs))(lists.foldl[Boolean,
     Boolean](logic.or)(false)(lists.map[hydra.core.Term, Boolean](hydra.ext.haskell.testCodec.containsTriviallyPolymorphic)(v_Term_list_xs)))
  case hydra.core.Term.set(v_Term_set_s) => logic.or(sets.`null`[hydra.core.Term](v_Term_set_s))(lists.foldl[Boolean,
     Boolean](logic.or)(false)(lists.map[hydra.core.Term, Boolean](hydra.ext.haskell.testCodec.containsTriviallyPolymorphic)(sets.toList[hydra.core.Term](v_Term_set_s))))
  case hydra.core.Term.map(v_Term_map_m) => logic.or(maps.`null`[hydra.core.Term, hydra.core.Term](v_Term_map_m))(logic.or(lists.foldl[Boolean,
     Boolean](logic.or)(false)(lists.map[hydra.core.Term, Boolean](hydra.ext.haskell.testCodec.containsTriviallyPolymorphic)(maps.keys[hydra.core.Term,
     hydra.core.Term](v_Term_map_m))))(lists.foldl[Boolean, Boolean](logic.or)(false)(lists.map[hydra.core.Term,
     Boolean](hydra.ext.haskell.testCodec.containsTriviallyPolymorphic)(lists.map[Tuple2[hydra.core.Term,
     hydra.core.Term], hydra.core.Term]((p: Tuple2[hydra.core.Term, hydra.core.Term]) => pairs.second[hydra.core.Term,
     hydra.core.Term](p))(maps.toList[hydra.core.Term, hydra.core.Term](v_Term_map_m))))))
  case hydra.core.Term.maybe(v_Term_maybe_mx) => maybes.maybe[Boolean, hydra.core.Term](true)(hydra.ext.haskell.testCodec.containsTriviallyPolymorphic)(v_Term_maybe_mx)
  case hydra.core.Term.either(v_Term_either__) => true
  case hydra.core.Term.union(v_Term_union_inj) => hydra.ext.haskell.testCodec.containsTriviallyPolymorphic(v_Term_union_inj.field.term)
  case hydra.core.Term.pair(v_Term_pair_p) => logic.or(hydra.ext.haskell.testCodec.containsTriviallyPolymorphic(pairs.first[hydra.core.Term,
     hydra.core.Term](v_Term_pair_p)))(hydra.ext.haskell.testCodec.containsTriviallyPolymorphic(pairs.second[hydra.core.Term,
     hydra.core.Term](v_Term_pair_p)))
  case hydra.core.Term.record(v_Term_record_rec) => lists.foldl[Boolean, Boolean](logic.or)(false)(lists.map[hydra.core.Field, Boolean]((f: hydra.core.Field) =>
    hydra.ext.haskell.testCodec.containsTriviallyPolymorphic(f.term))(v_Term_record_rec.fields))
  case hydra.core.Term.application(v_Term_application_app) => logic.or(hydra.ext.haskell.testCodec.containsTriviallyPolymorphic(v_Term_application_app.function))(hydra.ext.haskell.testCodec.containsTriviallyPolymorphic(v_Term_application_app.argument))
  case _ => false

def buildTestModuleWithCodec[T0](codec: hydra.testing.TestCodec)(testModule: hydra.module.Module)(testGroup: hydra.testing.TestGroup)(testBody: scala.Predef.String)(namespaces: T0): scala.Predef.String =
  {
  val `ns_`: hydra.module.Namespace = (testModule.namespace)
  val specNs: hydra.module.Namespace = strings.cat2(`ns_`)("Spec")
  val moduleNameString: scala.Predef.String = codec.formatModuleName(specNs)
  val `groupName_`: scala.Predef.String = (testGroup.name)
  val domainImports: Seq[scala.Predef.String] = codec.findImports(sets.empty[hydra.core.Name])
  val standardImports: Seq[scala.Predef.String] = Seq("import Hydra.Kernel", "import qualified Test.Hspec as H",
     "import qualified Data.List as L", "import qualified Data.Map as M", "import qualified Data.Set as S",
     "import qualified Data.Maybe as Y")
  val allImports: Seq[scala.Predef.String] = lists.concat2[scala.Predef.String](standardImports)(domainImports)
  val debugComments: Seq[scala.Predef.String] = Seq("-- DEBUG: Focus namespace = (see generated module)",
     "-- DEBUG: Namespace mappings: (see generated module)")
  val header: scala.Predef.String = strings.intercalate("\n")(lists.concat[scala.Predef.String](Seq(Seq(strings.cat2("-- ")(hydra.constants.warningAutoGeneratedFile),
     ""), debugComments, Seq("", strings.cat(Seq("module ", moduleNameString, " where")), ""), allImports,
     Seq("", "spec :: H.Spec", strings.cat(Seq("spec = H.describe ", literals.showString(`groupName_`),
     " $ do"))))))
  strings.cat(Seq(header, "\n", testBody, "\n"))
}

